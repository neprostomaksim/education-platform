## Фотосессия и AI-модель

Самое ценное — **серия кадров одного и того же персонажа или продукта.** Так и делаются нейрофотосессии.

### Правило консистентности

1. Опишите субъект **один раз и максимально детально** — это «карточка персонажа»: возраст, лицо, волосы, телосложение, одежда, свет.
2. Переиспользуйте эту карточку **дословно** в каждом кадре.
3. Меняйте **одну ось за раз**: поза ИЛИ ракурс ИЛИ одежда ИЛИ локация ИЛИ свет. Остальное держите неизменным.
4. Формулировка кадра: «тот же [субъект], что на кадре 1, но [одно изменение]».

### AI-модель без «пластикового» вида

- Просите **фотореализм с фотопризнаками:** камера, объектив (85mm), плёнка (Kodak Portra 400), мягкий свет, зерно, малая глубина резкости — это «оживляет» кожу и свет.
- Добавляйте естественные несовершенства (текстура кожи, живой свет), избегайте «глянцевого 3D» без нужды.
- **Нативность = охват:** дешёво-нейросетевой вид режет охваты (правило из Занятия 1), поэтому реализм важен.

### Как одеть модель и добавить объекты

- **Одежда:** опишите крой, ткань и цвет — или дайте **референс-изображение** одежды и укажите «надень одежду с изображения 2 на персонажа с изображения 1».
- **Продукт/объект в кадре:** дайте фото товара референсом и попросите «модель держит [продукт с изображения 2] в руке», указав, что и модель, и продукт нужно сохранить узнаваемыми.

