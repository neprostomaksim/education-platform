## Главный инструмент: ИИ арт-директор + Nano Banana 2

Писать такой промпт каждый раз вручную — долго. Поэтому вы соберёте **бота-арт-директора**: пишете обычными словами «сделай фото бариста с кофе», а он сам определяет тип задачи, добавляет свет, композицию и стиль, выдаёт готовый промпт — и по вашему согласию сразу генерит.

### Что умеет Nano Banana 2 (Gemini 3.1 Flash Image)

- **Правки словами** (семантическое редактирование): «убери людей на фоне, небо сделай закатным» — модель сама находит объект. Всегда указывайте, **что менять и что оставить.**
