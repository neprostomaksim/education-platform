## Первые кадры для видео

На следующих занятиях мы оживим картинки в видео (Kling, Veo, HeyGen). Для этого нужен **стартовый кадр** — и он делается **не как «красивое фото»**, а по своим правилам:

1. **Чисто и резко**, без смазов — движение добавит видеомодель.
2. **Оставьте место для движения:** если субъект/камера пойдут в сторону — не обрезайте по краю, дайте «воздух».
3. **Субъект чётко отделён от фона**, сцена не перегружена.
4. **Кадр = начало действия** («рука только пошла вперёд»), а не финал.
5. **Свет нейтральный**, без экстремального контрового (он мерцает при анимации).
6. **Формат под видео** — 16:9 или 9:16.

Ваш агент это умеет: он определит тип «стартовый кадр для видео» и соберёт кадр по этим правилам, добавив заметку по движению.

