## Сторис и карусели

Форматы вертикальные: сторис и карусели — **9:16** (или 4:5 для ленты). Карусели работают на **сохранения**: обложка + польза по слайдам.

### Два способа собрать карусель или сторис

| Способ | Когда удобно | Как делаем |
|---|---|---|
| **1. Полностью в нейросети** (Nano Banana 2 / ChatGPT Image 2) | быстро, единый стиль, текста немного | генерируете слайд целиком — фон + надпись. Текст пишете **дословно в кавычках**, с расположением и иерархией |
| **2. Фон в нейросети + текст в редакторе** (Figma / Canva) | нужен аккуратный или длинный текст, точная типографика, единый шаблон | генерируете **только фоновую картинку**, а заголовки и подписи добавляете поверх в Figma/Canva |

Способ 1 — быстрее, способ 2 — больше контроля над текстом и шрифтами. Для плотного текста удобнее редактор; для быстрых визуалов — генерация целиком.

### Текст на картинке (для способа 1)

- Пишите текст **дословно в кавычках:** «жирная белая надпись „КАК Я ЭКОНОМЛЮ 2 ЧАСА В ДЕНЬ“ в центре, на тёмной плашке».
- Держите **иерархию:** заголовок / подзаголовок / акцент.
- Не перегружайте: 1–2 текстовых блока на обычный слайд; больше — только для инфографики.

`[СКРИН: пример карусели — слайд, собранный в нейросети, и слайд с текстом поверх фона в Canva]`

### 🛠 Практика 5. Карусель

