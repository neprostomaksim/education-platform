## Сторис и карусели

Форматы вертикальные: сторис и карусели — **9:16** (или 4:5 для ленты). Карусели работают на **сохранения**: обложка + польза по слайдам.

### Два способа собрать карусель или сторис

| Способ | Когда удобно | Как делаем |
|---|---|---|
| **1. Полностью в нейросети** (Nano Banana 2 / ChatGPT Image 2) | быстро, единый стиль, текста немного | генерируете слайд целиком — фон + надпись. Текст пишете **дословно в кавычках**, с расположением и иерархией |
| **2. Фон в нейросети + текст в редакторе** (Figma / Canva) | нужен аккуратный или длинный текст, точная типографика, единый шаблон | генерируете **только фоновую картинку**, а заголовки и подписи добавляете поверх в Figma/Canva |

Способ 1 — быстрее, способ 2 — больше контроля над текстом и шрифтами. Для плотного текста удобнее редактор; для быстрых визуалов — генерация целиком.

### Текст на картинке (для способа 1)

- Пишите текст **дословно в кавычках:** «жирная белая надпись „КАК Я ЭКОНОМЛЮ 2 ЧАСА В ДЕНЬ“ в центре, на тёмной плашке».
- Держите **иерархию:** заголовок / подзаголовок / акцент.
- Не перегружайте: 1–2 текстовых блока на обычный слайд; больше — только для инфографики.

`[СКРИН: пример карусели — слайд, собранный в нейросети, и слайд с текстом поверх фона в Canva]`

### Агент: текст для карусели

**Что делает:** из вашей идеи собирает готовый текст поста-карусели по слайдам — обложка-хук + слайды по одной мысли, без перегруза. Текст потом копируете в Figma/Canva или отдаёте в Nano Banana 2.

```prompt
Ты — копирайтер контента для соцсетей. Из моей идеи собери ТЕКСТ для
поста-карусели, готовый к вёрстке.

Идея / тема: [ОПИШИ ИДЕЮ ИЛИ ТЕМУ]
Ниша и аудитория (по желанию): [ОПИШИ, ДЛЯ КОГО И О ЧЁМ]

Правила:
- Обложка (слайд 1) — цепляющий заголовок-хук, до ~7 слов; бьёт в боль,
  выгоду или любопытство и заставляет листать дальше и сохранить.
- 1 слайд = 1 мысль. Не сваливай несколько идей в один слайд.
- Мало текста: на слайд — короткий заголовок (до 6 слов) + 1–2 коротких
  предложения (до ~25 слов всего). Слайд не должен быть перегружен.
- Логика: обложка → 4–6 слайдов пользы (по одному пункту на слайд) →
  финальный слайд с призывом (сохранить, подписаться, написать в директ).
- Язык живой, без воды и канцелярита.

Выдай результат строго по слайдам, чтобы было удобно копировать:

СЛАЙД 1 (обложка)
Заголовок: ...

СЛАЙД 2
Заголовок: ...
Текст: ...

(и так далее — до финального слайда с призывом)
```

**Как пользоваться:** вставьте идею → получите текст по слайдам → дальше два пути: **(1)** скопируйте заголовки и тексты в Figma/Canva поверх фонов; **(2)** отдайте текст в Nano Banana 2 и попросите собрать слайды с этими надписями **дословно** (способ 1 из таблицы выше).

**Пример:** идея «как выбрать первый ноутбук» → обложка «5 ошибок при покупке ноутбука» → слайды по одной ошибке → финал «Сохрани, чтобы не переплатить».

