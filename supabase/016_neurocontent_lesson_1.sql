### Агент 1. Генератор 15 сценариев из транскрипта

**Что делает:** берёт текст чужого удачного ролика и вашу нишу — выдаёт 15 готовых сценариев под ваш блог, заточенных под удержание.

```prompt
Ты — стратег по контенту для соцсетей, работающий по актуальным правилам
алгоритмов 2026 года.

Я дам тебе транскрипт чужого успешного ролика. Используй только его РАБОЧУЮ
СТРУКТУРУ (как построен хук, как удерживается внимание, чем цепляет) — но НЕ
копируй смысл. На её основе предложи 15 НОВЫХ идей для коротких видео
(Reels / Shorts / TikTok) под мою нишу.

Моя ниша и аудитория: [ОПИШИ СВОЮ НИШУ И ДЛЯ КОГО ТЫ СНИМАЕШЬ]

Правила, которым ты строго следуешь:
- Все 15 идей — в рамках ОДНОЙ моей ниши, без скачков по темам.
- Для каждой идеи дай ровно 5 пунктов:
  1) Тема ролика
  2) Хук — первая фраза или первый кадр, который удержит за 3 секунды
  3) Почему ролик захотят досмотреть до конца
  4) Что заставит его переслать другу или сохранить
  5) Формат (говорящая голова / съёмка процесса / карусель / текст на экране)
- Хук бьёт в боль, любопытство или выгоду зрителя. Никаких «всем привет».
- Никакой воды и общих фраз. Только конкретика под нишу.

Выдай 15 идей нумерованным списком.

Транскрипт ролика:
[ВСТАВЬ ТЕКСТ ТРАНСКРИПТА]
```

**Как пользоваться:** вставьте промпт в нейросеть → замените нишу и транскрипт → получите 15 сценариев. Слабые вычеркните, сильные — в свой банк идей.

**Пример заглушки:** `Ниша: домашняя выпечка на заказ, для женщин 25–45, которые хотят красивый торт на праздник.`

> **Доступ из РБ:** работает в любой нейросети с прямым доступом (ChatGPT, Gemini или DeepSeek как запасной).

### Агент 2. Уникализатор сценария

**Что делает:** переписывает один чужой транскрипт в оригинальный авторский сценарий — сохраняет рабочую структуру, но меняет подачу под вашу нишу и ваш голос. Это защита от «неоригинальности», которую душит площадка.

```prompt
Ты — сценарист коротких видео. Твоя задача — переписать чужой ролик в
ОРИГИНАЛЬНЫЙ авторский сценарий, а не сделать репост или пересказ.

Я дам транскрипт чужого ролика. Сделай так:
- Сохрани рабочую СТРУКТУРУ (сильный хук в первые 3 секунды, темп, развязку).
- Полностью поменяй подачу, примеры и формулировки под мою нишу и мой голос.
- Убери всё, что привязывает ролик к чужому автору или другой площадке.
- Результат должен звучать живо и естественно, как будто это написал человек.

Моя ниша и аудитория: [ОПИШИ СВОЮ НИШУ]
Мой стиль/голос (по желанию): [НАПРИМЕР: простой, дружелюбный, с юмором]

Выдай готовый сценарий в формате:
1) Хук (первая фраза / первый кадр)
2) Основная часть по репликам/кадрам
3) Финал + повод сохранить или переслать

Транскрипт чужого ролика:
[ВСТАВЬ ТЕКСТ ТРАНСКРИПТА]
```

**Как пользоваться:** вставьте промпт → подставьте нишу и транскрипт → получите свою уникальную версию. Прочитайте вслух: если звучит как вы — готово.

> **Доступ из РБ:** работает в любой доступной нейросети.

### Агент 3. Разбор успешного ролика

**Что делает:** берёт чужой залетевший ролик, объясняет, *почему* он сработал по сигналам 2026, и адаптирует идею под вас — с готовыми хуками.

```prompt
Ты — аналитик коротких видео. Ты разбираешь чужой успешный ролик по правилам
алгоритмов 2026 года и помогаешь автору сделать такой же сильный на своём
материале.

Я дам описание или текст успешного ролика. Разбери его по пунктам:
1. Хук: что в первые 3 секунды удержало зрителя и почему это сработало?
2. Удержание: за счёт чего ролик досматривают до конца? (структура, темп, интрига)
3. Распространение: что заставляет переслать его в личку или сохранить?
4. Формат и подача: как это снято и почему именно так.

Затем адаптируй идею под мою нишу: [ОПИШИ СВОЮ НИШУ]

Дай 3 варианта, как мне сделать похожий по силе ролик на своём материале.
Для каждого варианта пропиши готовый хук (первую фразу или первый кадр).

Ролик для разбора:
[ВСТАВЬ ОПИСАНИЕ ИЛИ ТЕКСТ РОЛИКА]
```

**Как пользоваться:** найдите залетевший ролик в своей нише → опишите его словами (или вставьте транскрипт) → подставьте нишу → получите разбор и 3 адаптированных идеи с хуками.

> **Доступ из РБ:** работает в любой доступной нейросети. Если не открывается основной сервис — собираем разбор на общем экране преподавателя.

### Плохой и хороший хук — почувствуйте разницу

Хук решает судьбу ролика. Вот как это выглядит на практике:

| ❌ Плохо (раскачка) | ✅ Хорошо (сразу в боль/выгоду) |
|---|---|
| «Всем привет, меня зовут Аня, сегодня расскажу про…» | «Вот почему ваш торт трескается сверху — и как это исправить за 1 минуту» |
| «В этом видео мы поговорим о маркетинге» | «Эта одна ошибка в описании сливает вам половину заявок» |

## Практика занятия

**База (обязательно):**
1. Настройте рабочие сервисы: VPN + регистрация в ChatGPT (или DeepSeek) и Gemini.
2. Найдите 2–3 залетевших ролика в своей нише.
3. По пайплайну (скачать → транскрибировать → агент) соберите **минимум 10 сценариев** для своего блога.

**Бонус (для тех, кто справился быстро):**
- Доведите банк до **15+ сценариев**.
- Прогоните 2–3 ролика через «Агент 3. Разбор успешного ролика».
- Накидайте черновой контент-план на неделю из лучших идей.

## Домашнее задание

Доберите свой банк идей до **15–20 сценариев**. Под каждым укажите **формат** (говорящая голова / процесс / карусель / текст на экране) и **хук** (первая фраза или первый кадр).

**Критерий «сделано»:** 15+ сценариев, у каждого прописаны формат и хук.

## Что важно запомнить

- 🧠 **ИИ — усилитель, а не заменитель.** Он работает вместе с вами и в разы быстрее, но смысл и проверка — на вас.
- 📲 Соцсеть показывает контент не всем подписчикам, а тем, кто его **досмотрит**. Совпадайте с её целью.
- 🎓 Каждый ролик сдаёт **экзамен** на маленькой аудитории. Первые 3 секунды решают всё.
- 🥇 Сигналы по силе: **досмотр → пересылки/сохранения → комментарии → лайки**.
- 🎯 5 правил: хук за 3 секунды · повод переслать/сохранить · одна ниша · нативность · регулярность.
- 🛠️ Идеи не выдумываем из головы — берём рабочую структуру залетевших роликов и **уникализируем под себя**.
- ✅ Главный результат сегодня: настроены сервисы и собран банк из 10–15 сценариев. Вопрос «о чём снимать» закрыт.
