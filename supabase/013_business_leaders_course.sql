## Промпт 2: Аналитическая записка

### Когда использовать
- Вы хотите обосновать перед руководством инвестицию, найм, смену подрядчика или запуск нового проекта.
- Нужно структурировать аргументы «за» и «против» решения, чтобы принять его на основе данных, а не интуиции.
- Коллега просит вас «подготовить обоснование» — и вы хотите сделать это профессионально за 20 минут.

### Готовый промпт

```prompt
Ты — стратегический консультант с опытом работы в McKinsey-формате
