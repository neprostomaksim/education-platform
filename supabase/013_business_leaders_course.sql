1. Для КАЖДОЙ из перечисленных компетенций составь блок интервью:
   a) Основной вопрос (behavioral, по методу STAR — просит описать конкретную ситуацию из прошлого опыта)
   b) 2 уточняющих follow-up вопроса (чтобы кандидат не отделался общими фразами)
