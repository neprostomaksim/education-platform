## Промпт 2: Анализ и оптимизация бизнес-процессов (Lean)

### Когда использовать

- Когда процесс работает медленно и все к этому привыкли — «у нас согласование договора занимает 2 недели, потому что так всегда было»
- При масштабировании — процессы, которые работали на 10 сотрудников, ломаются на 50. Нужно оптимизировать до того, как они станут bottleneck
- При подготовке к автоматизации — прежде чем автоматизировать процесс, нужно его очистить от мусора, иначе вы автоматизируете хаос

### Готовый промпт

```prompt
Ты — Lean-консультант с сертификацией Six Sigma Black Belt
и опытом оптимизации процессов в 50+ компаниях. Проведи
анализ бизнес-процесса и предложи план оптимизации.

<process_description>
Название процесса: {process_name}
Компания/отдел: {department}
Цель процесса: {process_goal}
Частота выполнения: {frequency}
Участники: {participants}

Текущие шаги процесса (as-is):
1. {step_1} — кто делает: {responsible_1}, время: {time_1}
2. {step_2} — кто делает: {responsible_2}, время: {time_2}
3. {step_3} — кто делает: {responsible_3}, время: {time_3}
... (добавьте все шаги)

Известные проблемы: {known_issues}
Среднее время выполнения от начала до конца: {total_time}
Количество ошибок/возвратов: {error_rate}
Стоимость одного прохождения процесса: {process_cost}
</process_description>

Проведи анализ по методологии Lean:

1. **Карта потока ценности (Value Stream Map)** —
   опиши текущий процесс as-is. Для каждого шага укажи:
   - Время выполнения (processing time)
   - Время ожидания (waiting time)
   - Добавляет ли ценность для клиента (VA / NVA / BNVA)
     VA = Value-Added, NVA = Non-Value-Added,
     BNVA = Business Non-Value-Added (не для клиента,
     но необходимо для бизнеса)

2. **Выявление потерь (8 видов Muda):**
   - Перепроизводство — делаем больше, чем нужно
   - Ожидание — простой между этапами
   - Транспортировка — лишние перемещения данных/документов
   - Излишняя обработка — делаем сложнее, чем необходимо
   - Запасы — накопление незавершённых задач
   - Лишние движения — ненужные действия сотрудников
   - Дефекты — ошибки, переделки, возвраты
   - Неиспользованный потенциал — сотрудники выполняют
     работу ниже своей квалификации

3. **Bottleneck-анализ** — определи шаг, который
   ограничивает пропускную способность всего процесса.
   Рассчитай, насколько его ускорение повлияет на общее
   время процесса.

4. **Целевой процесс (to-be)** — опиши оптимизированный
   процесс. Для каждого изменения: что убрали/изменили,
   почему, ожидаемый эффект.

5. **Расчёт эффекта:**
   - Сокращение времени: с X до Y (экономия Z%)
   - Сокращение ошибок: с X% до Y%
   - Финансовый эффект: экономия N руб/мес
   - Высвобождение ресурсов: N часов/мес

6. **План внедрения** — поэтапный план перехода
   от текущего процесса к целевому. Кто ответственный,
   сроки, риски, критерии успеха.

Формат: структурированный отчёт с таблицами.
Используй визуальные маркеры для типов потерь.
```

### Как адаптировать под свой бизнес

1. **Опишите процесс максимально подробно** — каждый шаг, каждое согласование, каждую передачу работы. То, что вам кажется «очевидным», может оказаться bottleneck
2. **Замерьте реальное время** — засеките секундомером время каждого шага и время ожидания между шагами. Часто 80% общего времени — это ожидание, а не работа
3. **Начните с самого болезненного процесса** — где больше всего жалоб, ошибок или задержек. Первая оптимизация должна дать видимый результат, чтобы команда поверила в подход
4. **Для IT-процессов** — добавьте блок `<system_landscape>` с описанием используемых систем и интеграций. Часто bottleneck — в ручном переносе данных между системами

### Пример использования

