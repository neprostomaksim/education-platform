## Формула РЗКОП: 5 элементов эффективного промпта

Запомните аббревиатуру **РЗКОП** — пять элементов, которые превращают слабый промпт в мощный:

### 1. 🎭 Роль (кто ты?)

Задайте ИИ роль эксперта, от лица которого он будет отвечать. Роль определяет тон, глубину, терминологию и фокус ответа.

**Плохо:** *«Расскажи про маркетинг»*
**Хорошо:** *«Ты — директор по маркетингу с 15-летним опытом в B2B SaaS-компаниях на российском рынке»*

Роль меняет всё. Один и тот же вопрос, заданный «финансовому аналитику» и «HR-директору», даст совершенно разные ответы — и оба будут ценными в своём контексте.

**Бизнес-пример:** Вы хотите оценить идею нового продукта. Попросите ИИ ответить последовательно от лица: финансового директора (окупаемость), директора по продажам (реализуемость), клиента (потребность). Три роли — три перспективы — полная картина.

### 2. 📋 Задача (что сделать?)

Чётко сформулируйте, что именно должен сделать ИИ. Используйте конкретные глаголы: «проанализируй», «составь список», «напиши», «сравни», «разработай».

**Плохо:** *«Что-нибудь про продажи»*
**Хорошо:** *«Разработай план увеличения конверсии отдела продаж на 15% за 3 месяца, включая конкретные шаги по неделям»*

**Правило одной задачи:** Один промпт — одна главная задача. Если вам нужно и проанализировать рынок, и написать стратегию, и подготовить презентацию — это три отдельных промпта (или разбейте на шаги внутри одного промпта).

### 3. 🔍 Контекст (что важно знать?)

Дайте ИИ информацию о вашей ситуации. Чем больше релевантного контекста — тем точнее ответ. Используйте XML-теги для структурирования данных.

**Плохо:** *«Как увеличить продажи?»*
**Хорошо:**
```
Контекст моей компании:
- Ниша: онлайн-образование для взрослых
- Средний чек: 45 000 ₽
- Текущая конверсия: 2,3% из заявки в оплату
- Команда продаж: 4 менеджера
- Главная проблема: клиенты уходят после первого звонка
```

**Приём «XML-конверт»:** Оборачивайте данные в теги, чтобы ИИ чётко отличал ваши данные от инструкций:

```
<данные_компании>
Выручка Q1: 12.4 млн ₽
Расходы Q1: 9.8 млн ₽
Команда: 23 человека
</данные_компании>
```

### 4. ⚠️ Ограничения (чего НЕ делать?)

Установите границы: формат ответа, объём, стиль, что исключить. Ограничения — это то, что отличает «поток сознания» от структурированного ответа.

**Примеры ограничений:**
- *«Ответ — не более 500 слов»*
- *«Формат: таблица с колонками: Действие | Срок | Ответственный | Бюджет»*
- *«Без общих фраз типа "необходимо повысить эффективность" — только конкретные действия»*
- *«Язык: деловой русский, без сленга и англицизмов»*
- *«Если не знаешь точных данных — напиши "требует проверки", а не придумывай цифры»*

### 5. 📝 Образцы (как должно выглядеть?)

Покажите ИИ пример желаемого результата. Это самый мощный элемент: один хороший пример стоит десяти абзацев объяснений.

**Пример:**
```
Формат ответа — как в этом примере:

📌 Рекомендация: Внедрить систему follow-up звонков
⏱ Срок внедрения: 2 недели
💰 Бюджет: 0 ₽ (перестройка процесса)
📈 Ожидаемый эффект: +8% к конверсии
🎯 Первый шаг: Настроить напоминания в CRM для звонка через 24 часа после первого контакта
```

