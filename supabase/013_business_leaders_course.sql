## Промпт 2: Формирование OKR для команды

### Когда использовать

- После утверждения стратегии — для каскадирования целей от уровня компании до каждого отдела
- В начале квартала — для постановки измеримых целей команде
- При внедрении OKR с нуля — чтобы получить качественный шаблон и не наступить на типичные ошибки

### Готовый промпт

```prompt
Ты — OKR-коуч, сертифицированный по методологии Джона Дорра.
Помоги сформировать систему OKR для компании, каскадируя
цели от уровня компании до уровня отделов.

<okr_context>
Стратегические цели компании на квартал: {company_goals}
Отделы/команды: {departments}
Количество сотрудников: {team_size}
Ключевые метрики бизнеса: {key_metrics}
Текущие значения метрик: {current_values}
Целевые значения метрик: {target_values}
Период: {quarter}
</okr_context>

Сформируй OKR по следующим правилам:

1. **Уровень компании** — 3-4 Objectives.
   Каждый Objective: вдохновляющая, качественная формулировка.
   Для каждого — 3-4 Key Results: измеримые, с числами,
   с дедлайнами. Формат KR: «Увеличить X с Y до Z к дате».

2. **Уровень отделов** — для каждого отдела из списка:
   2-3 Objectives, каскадированных от целей компании.
   Покажи связь: «Поддерживает Company OKR #N».
   Для каждого — 3 Key Results.

3. **Матрица зависимостей** — таблица, какие OKR отделов
   зависят друг от друга. Формат: OKR отдела → зависит от →
   OKR другого отдела.

4. **Чек-лист качества OKR** — проверь каждый KR по критериям:
   измеримость, амбициозность (70% вероятность достижения),
   независимость от внешних факторов, отсутствие задач
   вместо результатов.

5. **План check-in** — предложи ритм еженедельных
   и ежемесячных проверок OKR: кто, когда, какой формат.

Типичные ошибки, которых нужно избежать:
- KR не должен быть задачей («Запустить рекламу»).
  Правильно: «Увеличить входящие лиды с 200 до 500/мес».
- Objective не должен содержать цифр.
- Не более 4 OKR на отдел — иначе фокус размывается.
```

### Как адаптировать под свой бизнес

1. **Перечислите все отделы**, для которых нужны OKR — ИИ создаст каскадную структуру. Если отдел маленький (1-3 человека), укажите это — ИИ скорректирует амбициозность
2. **Добавьте текущие OKR**, если они уже есть, в блок `<current_okrs>` — ИИ предложит корректировки вместо создания с нуля
3. **Укажите уровень зрелости команды** — если OKR внедряются впервые, добавьте: «Команда не работала с OKR раньше, нужны простые и понятные формулировки»
4. **Для каждого отдела** вы можете запустить промпт отдельно, добавив детали: текущие проекты, размер команды, специфические ограничения

### Пример использования

Заполненные переменные:
- `{company_goals}` → «1) Рост выручки на 60% 2) Выход на новый сегмент B2B 3) Снижение оттока до <5%»
- `{departments}` → «Продажи (8 чел), Маркетинг (4 чел), Продукт (6 чел), Поддержка (3 чел)»
- `{quarter}` → «Q3 2026»

Фрагмент ответа ИИ:

> **Company Objective #1:** Стать лидером в B2B-сегменте региона
> - KR 1.1: Закрыть 15 Enterprise-сделок с ARPU >200 тыс. руб/мес к 30.09
> - KR 1.2: Достичь NPS ≥60 среди B2B-клиентов к 30.09
> - KR 1.3: Сократить цикл продаж B2B с 45 до 25 дней
>
> **Отдел продаж → Objective 1** (Поддерживает Company OKR #1):
> Выстроить предсказуемый pipeline Enterprise-продаж

