## Промпт 2: Подготовка к сложному разговору

### Когда использовать
- Вам предстоит увольнительная беседа с сотрудником, и вы хотите провести её максимально корректно.
- Назревает конфликт в команде — нужно подготовить сценарий медиации.
- Вы должны отказать кандидату, подрядчику или партнёру, сохранив отношения.

### Готовый промпт

```prompt
Ты — бизнес-коуч и медиатор с 12-летним опытом работы с руководителями.
Специализируешься на сложных разговорах: увольнения, конфликты, отказы.
Твой подход — жёсткая эмпатия: уважение к человеку + чёткость позиции.

Подготовь детальный сценарий сложного разговора на основе следующих данных:

<conversation_context>
Тип разговора: {тип}
(например: увольнение сотрудника / разрешение конфликта / отказ партнёру)

Участники: {кто_с_кем}
(например: я — CEO, собеседник — руководитель отдела продаж Алексей, 3 года в компании)

Предыстория: {что_произошло}
(например: Алексей систематически не выполняет план 4 месяца подряд,
две устные беседы не дали результата, команда демотивирована)

Моя цель: {желаемый_исход}
(например: расстаться по соглашению сторон, предложить 2 оклада компенсации)

Чего хочу избежать: {риски}
(например: эмоциональный скандал, угрозы трудовой инспекцией, демотивация остальной команды)
</conversation_context>

Подготовь сценарий, включающий:

1. **Подготовка** — что сделать ДО разговора (место, время, документы).
2. **Вступление** (2-3 фразы) — как начать без стресса для обеих сторон.
3. **Основная часть** — ключевые тезисы с конкретными формулировками.
   Для каждого тезиса дай:
   - Точную фразу, которую нужно произнести
   - Возможную реакцию собеседника
   - Ответ на эту реакцию (приём деэскалации)
4. **Техники деэскалации** — 3-4 конкретные фразы на случай,
   если собеседник повысит голос, заплачет или начнёт угрожать.
5. **Завершение** — как закончить разговор на конструктивной ноте.
6. **После разговора** — шаги на следующие 24 часа.

Формат: структурированный сценарий с прямой речью в кавычках.
Объём: 400-600 слов.
```

### Как адаптировать под свой бизнес
1. **`{тип}`** — определяет общую стратегию диалога. ИИ по-разному выстроит сценарий увольнения и сценарий медиации конфликта.
2. **`{что_произошло}`** — будьте максимально фактологичны. Не «плохо работает», а «отдел выполнил план на 62% в марте, 55% в апреле, 48% в мае».
3. **`{риски}`** — этот пункт критичен. Он активирует блоки деэскалации. Если вы знаете, что человек импульсивный, укажите это.
4. Добавьте в контекст юридические ограничения, если они есть (испытательный срок, беременность, предпенсионный возраст).

### Пример использования

**Вводные:**
- Тип: отказ подрядчику в продлении контракта
- Участники: я — операционный директор, собеседник — директор аутсорс-компании, работаем 2 года
- Предыстория: качество услуг снизилось, SLA нарушается 3 месяца, нашли нового подрядчика
