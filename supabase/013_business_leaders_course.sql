## Что такое языковая модель простыми словами

Представьте, что вы наняли **блестящего стажёра**. Он прочитал всё: все книги по менеджменту, все отчёты консалтинговых компаний, все статьи в Harvard Business Review, все форумы, все юридические документы. У него феноменальная память и невероятная скорость обработки текста.

Но есть одна проблема: **он никогда не работал в реальном бизнесе**. У него нет опыта, нет интуиции, нет понимания вашей корпоративной культуры. Он не знает, что Иван Петрович из отдела закупок всегда завышает сроки на 30%, а ваш ключевой клиент не любит формальный тон.

Именно так работает языковая модель:

- **Она обучена на огромном массиве текстов** — миллиарды страниц из интернета, книг, статей
- **Она предсказывает следующее слово** — буквально: «Какое слово с наибольшей вероятностью должно идти дальше?»
- **Она не понимает смысл** в человеческом смысле слова — она распознаёт паттерны и закономерности в языке

Когда вы спрашиваете ChatGPT «Как повысить продажи?», он не анализирует ваш бизнес. Он генерирует текст, который *статистически похож* на хорошие ответы о продажах из миллионов текстов, на которых обучался.

