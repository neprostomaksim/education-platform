1. ПОЗИЦИОНИРОВАНИЕ
   - Сформулируй Unique Value Proposition (UVP) — одно предложение, которое отвечает на вопрос «Почему клиент выберет нас, а не конкурента?»
   - Карта позиционирования: таблица | Наш продукт | Конкурент 1 | Конкурент 2 | по параметрам: цена, качество, скорость, сервис, уникальная фича
   - Messaging framework: ключевое сообщение для каждого сегмента аудитории

2. КАНАЛЫ И ТАКТИКИ
   Для каждого рекомендуемого канала:
   | Канал | Почему именно он | Формат контента | Бюджет (% от общего) | Ожидаемый результат за 3 мес. |
   Приоритизируй: сначала каналы с быстрой отдачей, затем долгосрочные.

3. КВАРТАЛЬНЫЙ ROADMAP
   Таблица по месяцам:
   | Месяц | Ключевые действия | Ответственный (роль) | KPI | Бюджет |

4. МЕТРИКИ И ДАШБОРД
   - 5-7 ключевых KPI с целевыми значениями
   - Формула расчёта стоимости привлечения клиента (CAC) для вашего случая
   - Предложение по частоте отчётности и формату дашборда

5. РИСКИ И ПЛАН Б
   Топ-3 риска с вероятностью, влиянием и конкретным action plan.

Ограничения:
- Бюджет строго в рамках указанного. Если бюджет мал — сосредоточься на бесплатных и low-cost каналах.
- Не предлагай каналы, которые требуют команды более 2-3 человек, если это стартап.
- Все цифры KPI должны быть реалистичными для указанной стадии бизнеса.
```

### Как адаптировать под свой бизнес
1. **{product}** — опишите не просто название, а что именно делает ваш продукт и какую проблему решает. Чем больше контекста — тем точнее стратегия.
2. **{business_stage}** — стадия бизнеса критически влияет на выбор каналов. На запуске важен product-market fit и первые 100 клиентов, на стадии роста — масштабирование каналов.
3. **{competitors}** — укажите 2-3 прямых конкурента с их сильными сторонами. Если не знаете конкурентов — так и напишите, ИИ предложит методику анализа.
4. **{budget}** — будьте честны. Если бюджет 50 000 рублей в месяц — промпт адаптирует рекомендации под эту реальность.

### Пример использования

**Заполненные переменные:**
- product: `Онлайн-платформа для корпоративного обучения сотрудников (LMS) с элементами геймификации`
- business_stage: `рост`
- target_audience: `HR-директора и L&D менеджеры компаний от 200 сотрудников`
- competitors: `iSpring (лидер рынка, дорогой), Teachbase (дешевле, но без геймификации), GetCourse (больше для инфобизнеса)`
- current_channels: `контекстная реклама Яндекс, холодные email-рассылки`
- budget: `800 000 руб./квартал`
- business_goal: `Удвоить количество платящих клиентов (с 40 до 80) за 6 месяцев`

**Фрагмент результата:**

> **UVP:** «Корпоративное обучение, которое сотрудники проходят до конца — LMS с геймификацией, где completion rate на 45% выше отраслевого стандарта.»
>
> **Канал #1:** LinkedIn + экспертный контент
> Почему: 73% HR-директоров используют LinkedIn для поиска решений. Формат: кейсы клиентов, ROI-калькулятор, сравнение с конкурентами. Бюджет: 25%. Результат: 150 MQL за 3 месяца.

