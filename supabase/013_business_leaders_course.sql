## Формат 1: Чат (ChatGPT, Claude, GigaChat)

**Что это:** Вы задаёте вопрос → ИИ отвечает → вы уточняете → он отвечает снова. Диалог в реальном времени, где вы управляете каждым шагом.

**Аналогия:** Это как разговор с консультантом. Вы спрашиваете — он отвечает. Вы направляете беседу, решаете, когда остановиться, и сами оцениваете качество ответа.

**Когда использовать:**
- Вам нужен черновик документа, письма, презентации
- Вы хотите провести мозговой штурм или получить идеи
- Нужно быстро разобраться в незнакомой теме
- Вы анализируете конкретный текст (отзывы, отчёт, договор)

**Пример из бизнеса:** Директор по маркетингу вставляет в чат отчёт об эффективности рекламных каналов и просит: «Выдели три канала с лучшим ROI и предложи, как перераспределить бюджет». Получает ответ, уточняет детали, корректирует — и за 15 минут имеет готовую рекомендацию.

## Формат 2: ИИ-агент

**Что это:** ИИ, который получает задачу и **самостоятельно** выполняет последовательность шагов для её решения. Он может искать информацию, вызывать инструменты, принимать промежуточные решения и выдавать финальный результат.

**Аналогия:** Это как поручить задачу ассистенту. Вы говорите: «Подготовь мне сравнительный анализ трёх поставщиков» — и он сам ищет информацию, сравнивает, оформляет. Вы получаете готовый результат, а не отвечаете на вопросы на каждом шаге.

**Когда использовать:**
- Задача состоит из нескольких последовательных шагов
- Нужна автоматизация повторяющегося процесса
- Требуется работа с внешними источниками данных (почта, CRM, базы данных)
- Вы хотите, чтобы ИИ работал без вашего постоянного участия

**Пример из бизнеса:** Агент ежедневно собирает упоминания вашей компании в СМИ и соцсетях, классифицирует их по тональности (позитивные / негативные / нейтральные), формирует сводку и отправляет вам в Telegram каждое утро к 9:00.

## Формат 3: Мультиагентная система

**Что это:** Несколько ИИ-агентов, работающих вместе. Каждый специализируется на своей задаче, и они передают результаты друг другу по цепочке — как конвейер на производстве.

**Аналогия:** Это как проектная команда. Один специалист исследует рынок, передаёт данные аналитику, тот готовит выводы для стратега, стратег формирует рекомендации. Каждый делает то, в чём силён.

**Когда использовать:**
- Процесс настолько сложный, что один агент не справляется качественно
- Нужны разные «экспертизы» на разных этапах (анализ, генерация, проверка)
- Вы автоматизируете сквозной бизнес-процесс от начала до конца
- Масштаб задачи требует параллельной обработки

**Пример из бизнеса:** Система обработки входящих заявок: Агент 1 (классификатор) определяет тип обращения → Агент 2 (аналитик) извлекает ключевые данные → Агент 3 (исполнитель) формирует ответ → Агент 4 (контролёр) проверяет качество и тон ответа. Всё происходит за 30 секунд вместо 2 часов ручной работы.

