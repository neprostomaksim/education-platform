## Промпт 1: Профиль кандидата и описание вакансии

### Когда использовать
- Вы открываете новую позицию и хотите описать её так, чтобы привлечь сильных кандидатов, а не поток нерелевантных откликов.
- Вам нужно синхронизировать ожидания между руководителем, HR и рекрутером — единый профиль снимает разночтения.
- Вы хотите учесть корпоративную культуру, а не просто скопировать описание с HeadHunter.

### Готовый промпт

```prompt
