Сильные стороны (по результатам оценки):
{strengths}

Зоны развития (по результатам оценки):
{development_areas}

Предпочтения в обучении: {learning_preferences}
Доступный бюджет на обучение: {training_budget}
</employee_profile>

На основе данных из <employee_profile> создай Индивидуальный план развития (IDP) на {plan_duration} месяцев.

Структура плана:

1. GAP-АНАЛИЗ
   Таблица: | Компетенция | Текущий уровень | Требуемый для целевой роли | Разрыв (gap) |
   Оцени каждую компетенцию по шкале: базовый → уверенный → продвинутый → экспертный.

2. ПЛАН РАЗВИТИЯ — для каждой зоны развития составь блок:
   - 🎯 Цель развития (SMART-формулировка)
   - 📚 Формат обучения (70-20-10):
     * 70% — обучение на рабочем месте (конкретные проекты, задачи, вызовы)
