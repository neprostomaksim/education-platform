3. **{desired_outcome}** — опишите, что именно вы хотите изменить: «чтобы сотрудник начал готовить agenda к встречам заранее», «чтобы повторил успешный подход на следующем проекте».
4. Если вам нужна обратная связь для удалённой команды, добавьте в контекст: «Команда работает удалённо, общение через Slack и Zoom».

