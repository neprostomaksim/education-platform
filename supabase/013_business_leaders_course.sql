## Почему ИИ «галлюцинирует» и что с этим делать

«Галлюцинации» — это когда ИИ уверенно выдаёт неверную информацию. Он может придумать несуществующую статистику, сослаться на несуществующее исследование или назвать выдуманный закон.

**Почему это происходит:**

Вернёмся к нашей аналогии со стажёром. Если вы спросите его: «Какой был объём продаж нашей компании в третьем квартале?» — он не скажет «Я не знаю». Он вспомнит, какие цифры обычно фигурируют в подобных отчётах, и выдаст *правдоподобный* ответ. Не правильный — правдоподобный.

Модель обучена генерировать убедительный текст. У неё нет механизма проверки: «А это правда?». Она оптимизирована на то, чтобы ответ *выглядел* хорошим, а не *был* верным.

**Три правила работы с галлюцинациями для руководителя:**

1. **Правило светофора.** 🟢 Зелёный — творческие задачи (мозговой штурм, черновики): галлюцинации не критичны. 🟡 Жёлтый — аналитические задачи (обзоры, структурирование): нужна выборочная проверка. 🔴 Красный — фактические данные (цифры, законы, имена): всегда проверяйте.

2. **Просите источники.** Добавляйте в промпт: «Если не уверен — напиши, что данные требуют проверки. Не придумывай статистику.»

3. **Используйте ИИ как соавтора, а не как оракула.** ИИ даёт отличную первую версию. Ваша экспертиза — финальная проверка и адаптация под реальность.

## Границы применимости ИИ в бизнесе

Подумайте об ИИ как об **усилителе**, а не **заменителе**:

- **ИИ усиливает** вашу способность обрабатывать информацию, генерировать идеи и создавать контент
- **ИИ не заменяет** вашу экспертизу, понимание контекста и способность принимать решения в условиях неопределённости

**Правило 80/20 для ИИ в бизнесе:** ИИ может сделать 80% рутинной работы за 20% времени. Но оставшиеся 20% — экспертная доводка, проверка, адаптация под контекст — это ваша зона ответственности. И именно эти 20% определяют качество результата.

