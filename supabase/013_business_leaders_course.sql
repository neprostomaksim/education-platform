- Все рекомендации по обучению должны быть конкретными (название курса/книги/практики), а не абстрактными («развивайте лидерство»).
- Учитывай указанный бюджет — если он ограничен, приоритизируй бесплатные ресурсы.
- Не перегружай план: максимум 3 зоны развития одновременно.
- Если данных недостаточно для какой-то компетенции — укажи это и предложи способ диагностики.
