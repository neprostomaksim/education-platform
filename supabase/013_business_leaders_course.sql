«Пирамида Минто»: сначала вывод, потом аргументы, потом данные.

Подготовь аналитическую записку для обоснования бизнес-решения:

Решение, которое нужно обосновать: {decision}
(например: перевести колл-центр на аутсорсинг / открыть второй склад
в Новосибирске / заменить CRM-систему / нанять Head of Product)

Контекст: {context}
(например: текущий колл-центр из 12 операторов обходится в 1.8 млн ₽/мес,
среднее время ответа 45 сек, CSI 3.8/5, наблюдается рост обращений на 20% кв/кв,
нанимать ещё людей некуда — офис не позволяет)

Доступные данные: {data}
(например: коммерческие предложения от 3 аутсорс-компаний,
бенчмарки по отрасли, текущая статистика колл-центра)

Адресат записки: {addressee}
(например: генеральный директор / совет директоров / финансовый директор)

Структура аналитической записки:

1. **Резюме и рекомендация** (первый абзац)
   - Чёткая формулировка: «Рекомендую [решение], потому что [3 причины]»
   - Ожидаемый эффект в деньгах или метриках

2. **Описание проблемы / возможности**
   - Текущая ситуация (цифры)
   - Почему статус-кво неприемлем
   - Что произойдёт, если ничего не делать (стоимость бездействия)

3. **Анализ вариантов**
   Сравни 2-3 варианта решения в таблице:
   | Критерий | Вариант A | Вариант B | Вариант C |
   Критерии: стоимость, срок внедрения, риски, влияние на качество,
   масштабируемость.

4. **Финансовое обоснование**
   - Инвестиции (единоразовые + ежемесячные)
   - Ожидаемая экономия или дополнительная выручка
   - Срок окупаемости (ROI / Payback period)
   - Таблица: P&L-эффект на 12 месяцев

5. **Риски и митигация**
   | Риск | Вероятность | Влияние | Меры снижения |
   3-5 ключевых рисков.

6. **План реализации**
   - 3-5 ключевых этапов с датами
   - Ответственные
   - Критерии успеха (Definition of Done)

7. **Запрос**
   - Конкретно: что нужно от адресата (одобрение, бюджет, ресурсы)
   - Дедлайн для принятия решения

Формат: бизнес-документ в стиле management consulting.
Объём: 500-800 слов.
Каждое утверждение подкреплено данными или расчётом.
Если данных недостаточно для точного расчёта — укажи допущения явно.
```

### Как адаптировать под свой бизнес
1. **`{decision}`** — формулируйте как действие, а не как вопрос. Не «стоит ли нам...», а «перевести колл-центр на аутсорсинг».
2. **`{data}`** — чем больше реальных данных вы дадите, тем точнее будет финансовое обоснование. Если данных мало, ИИ предупредит о допущениях.
