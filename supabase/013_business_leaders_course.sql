Выполни следующие шаги последовательно:

Шаг 1. Составь «Портрет идеального кандидата» — таблицу с колонками:
| Критерий | Обязательно (must have) | Желательно (nice to have) |
Включи: hard skills, soft skills, опыт, личностные качества, культурный fit.

Шаг 2. На основе портрета напиши текст вакансии для размещения на job-борде. Структура:
- Заголовок (цепляющий, без канцеляризмов)
- О компании (2-3 предложения, подчеркни культуру)
- Чем предстоит заниматься (5-7 конкретных задач, начинай с глаголов действия)
