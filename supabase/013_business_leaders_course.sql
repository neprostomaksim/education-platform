     * 10% — формальное обучение (курсы, книги, конференции)
   - 📅 Таймлайн с промежуточными чекпоинтами (ежемесячно)
   - 📊 Метрики успеха — как конкретно измерить, что компетенция развита

3. QUICK WINS — 3 действия, которые сотрудник может начать в первые 2 недели, чтобы сразу почувствовать прогресс.

4. ПОДДЕРЖКА РУКОВОДИТЕЛЯ — что конкретно должен делать руководитель (ты), чтобы план сработал. Включи: частоту check-in, конкретные вопросы для обсуждения на 1-on-1.

