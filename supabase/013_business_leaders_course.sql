Вы уже знаете формулу эффективного промпта. Теперь — пять правил, которые отделяют руководителей, получающих от ИИ посредственные ответы, от тех, кто получает результаты уровня топ-консалтинга. Эти правила основаны на анализе тысяч реальных бизнес-промптов и типичных ошибок, которые совершают даже опытные управленцы.

Каждое правило проиллюстрировано примером «до и после» — вы увидите, как одно изменение в промпте кардинально меняет качество ответа.

## Правило 1: Будьте конкретны — расплывчатый запрос = расплывчатый ответ

ИИ не умеет читать мысли. Если вы написали «помоги с продажами», он не знает: вам нужен скрипт для холодных звонков? Анализ воронки? Стратегия выхода на новый рынок? Он выберет что-то наугад — и скорее всего не то, что вам нужно.

**Правило:** Чем конкретнее запрос, тем полезнее ответ. Указывайте: что именно, для кого, в каком формате, с какими ограничениями.

❌ **Плохо:**
```
Помоги улучшить работу отдела продаж
```

✅ **Хорошо:**
```
Проанализируй три основные причины, почему менеджеры
по продажам в B2B-компании теряют сделки на этапе
коммерческого предложения, и предложи по одному
конкретному решению для каждой причины.
Формат: таблица (Причина | Решение | Как внедрить за неделю)
```

**Почему это работает:** Конкретный запрос сужает «область поиска» для ИИ. Вместо того чтобы генерировать всё подряд на тему продаж, он фокусируется на одном конкретном этапе и выдаёт применимый результат.

## Правило 2: Задавайте формат ответа — контролируйте результат

Если вы не скажете ИИ, в каком формате отвечать, он выберет сам. Обычно это «стена текста» на 2 000 слов, из которой вам придётся самому вытаскивать суть. Это пустая трата времени.

**Правило:** Всегда указывайте формат: таблица, список, пошаговый план, email, слайды.

❌ **Плохо:**
```
Расскажи о трендах в ритейле
```

✅ **Хорошо:**
```
Назови 5 главных технологических трендов в российском
ритейле на 2025–2026 год. Формат ответа:

| # | Тренд | Суть (1 предложение) | Кто уже внедрил в России | Как может использовать средний бизнес |
```

**Варианты форматов для бизнес-задач:**
- **Таблица** — для сравнений, анализа, планов
- **Нумерованный список** — для рекомендаций, шагов
- **Email** — для готовых писем клиентам, партнёрам
- **Структура слайдов** — для подготовки презентаций
- **SWOT/матрица** — для стратегического анализа
- **FAQ** — для ответов на типовые вопросы

## Правило 3: Устанавливайте ограничения — без рамок нет качества

Ограничения — это не прихоть, это инструмент качества. Без них ИИ пишет «обо всём и ни о чём». С ними — фокусируется на том, что вам действительно нужно.

**Правило:** Добавляйте 2–4 ограничения к каждому важному промпту.

❌ **Плохо:**
```
Напиши описание вакансии руководителя отдела маркетинга
```

✅ **Хорошо:**
```
Напиши описание вакансии руководителя отдела маркетинга.

Ограничения:
- Компания: EdTech-стартап, 40 человек, Москва
- Объём: не более 300 слов
- Тон: дружелюбный, но профессиональный (не корпоративный)
- Обязательно включи: зарплатную вилку 250–350 тыс. ₽,
  возможность удалённой работы, KPI первых 3 месяцев
- НЕ включай: стандартные фразы вроде «стрессоустойчивость»
  и «коммуникабельность»
```

**Типы полезных ограничений:**
- **Объём:** «не более 200 слов», «3–5 пунктов»
- **Аудитория:** «для CEO без технического бэкграунда»
- **Тон:** «деловой, но не формальный», «как в Telegram-канале для предпринимателей»
- **Исключения:** «без общих советов», «не упоминай конкурентов по имени»
- **Антигаллюцинации:** «если не уверен в факте — явно укажи это»

## Правило 4: Итерируйте — первый ответ редко бывает лучшим

Самая распространённая ошибка руководителей: получить первый ответ от ИИ, решить, что «ИИ не работает», и закрыть чат. Это всё равно что прочитать первый черновик сотрудника и уволить его, не дав шанса доработать.

**Правило:** Работайте с ИИ как с сотрудником — давайте обратную связь и просите доработать.

❌ **Плохо:**
```
Первый ответ не понравился → закрываю чат → «ИИ бесполезен»
```

✅ **Хорошо — цикл итерации:**
```
Запрос 1: «Разработай план запуска нового продукта»
Ответ ИИ: [получаете первую версию]

Запрос 2: «Хорошо, но добавь бюджет к каждому этапу
и учти, что у нас ограничение — 2 млн ₽ на весь запуск»

Запрос 3: «Этап 3 слишком общий — разбей его на
конкретные подзадачи с ответственными»

Запрос 4: «Отлично. Теперь переформатируй весь план
в таблицу для презентации совету директоров»
```

**Три волшебные фразы для итерации:**
1. *«Хорошо, но сделай конкретнее пункт X — добавь цифры и сроки»*
2. *«Перепиши это с точки зрения [другой роли/аудитории]»*
3. *«Оставь только 3 самых важных пункта и обоснуй, почему именно они»*

## Правило 5: Разбивайте сложные задачи на шаги

Если вы просите ИИ одновременно «проанализируй рынок, разработай стратегию, напиши план, подготовь презентацию и рассчитай бюджет» — качество каждого элемента будет низким. ИИ, как и человек, лучше справляется с задачами, когда работает над ними последовательно.

**Правило:** Разбивайте большую задачу на 3–5 последовательных промптов.

❌ **Плохо:**
```
Проанализируй рынок доставки еды, разработай стратегию
выхода, подготовь финмодель, напиши план запуска
и текст для лендинга
```

✅ **Хорошо — цепочка промптов:**
```
Промпт 1: «Проанализируй рынок доставки еды в городах
с населением 300–500 тыс. Формат: SWOT-анализ»

Промпт 2: «На основе этого SWOT предложи 3 стратегии
выхода на рынок с разным уровнем инвестиций»

Промпт 3: «Возьми стратегию #2 и разработай финансовую
модель на 12 месяцев. Формат: помесячная таблица»

Промпт 4: «На основе стратегии #2 напиши план запуска
на первые 90 дней: таблица Неделя | Действие | Бюджет»

Промпт 5: «Напиши текст лендинга для этого проекта,
используя ключевые преимущества из стратегии»
```

**Почему это работает:** Каждый следующий промпт строится на результате предыдущего. ИИ помнит контекст разговора и углубляет анализ, вместо того чтобы размазывать внимание на всё сразу.

