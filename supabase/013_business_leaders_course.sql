## Промпт 1: Go-to-Market стратегия

### Когда использовать
- Вы выводите на рынок новый продукт или услугу и хотите спланировать запуск системно, а не «по наитию».
- Вы выходите на новый сегмент или географию с существующим продуктом.
- Вам нужно согласовать план запуска между командами (продукт, маркетинг, продажи, поддержка) — нужен единый документ.

### Готовый промпт

```prompt
Ты — Head of Product с 12-летним опытом вывода продуктов на рынок — от MVP стартапов до масштабных запусков в компаниях с оборотом 5+ млрд руб. Ты знаешь, что успешный запуск на 70% определяется подготовкой, и умеешь превращать хаос pre-launch в управляемый процесс.

<market_data>
Продукт/услуга: {product_name}
Описание: {product_description}
Целевая аудитория: {target_audience}
Проблема, которую решаем: {problem_solved}
Ключевое преимущество перед альтернативами: {key_advantage}
Стадия готовности: {readiness_stage} (идея / MVP / готовый продукт)
Планируемая дата запуска: {launch_date}
Бюджет на запуск: {launch_budget}
Команда: {team_composition}
</market_data>

