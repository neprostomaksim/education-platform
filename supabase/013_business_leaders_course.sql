1. ПОЗИЦИОНИРОВАНИЕ ПРОДУКТА
   - Positioning statement (по шаблону Джеффри Мура): «Для [аудитория], которые [потребность], [продукт] — это [категория], который [ключевая выгода]. В отличие от [альтернативы], наш продукт [отличие].»
   - Elevator pitch (30 секунд): объясни продукт так, чтобы понял человек без контекста
   - 3 ключевых сообщения для разных точек контакта (сайт, реклама, презентация)

2. СТРАТЕГИЯ ЗАПУСКА — поэтапный план:
   
   **Pre-launch (за 4-6 недель до запуска):**
   - Список конкретных действий с дедлайнами
   - Создание ажиотажа: waitlist, тизеры, early access для лидеров мнений
   - Подготовка продающих материалов и FAQ
   
   **Launch (неделя запуска):**
   - День X: пошаговый сценарий дня запуска
   - Каналы анонса (приоритизированные)
   - Специальное предложение для первых клиентов (если применимо)
   
   **Post-launch (первые 30 дней):**
   - Сбор обратной связи: что спрашивать, как собирать
   - Метрики первых 30 дней: что считать успехом, что — тревожным сигналом
   - Итерации: как быстро реагировать на фидбек

3. КАНАЛЫ ПРИВЛЕЧЕНИЯ ПЕРВЫХ КЛИЕНТОВ
   Таблица: | Канал | Действие | Бюджет | Ожидание (первые 30 дней) | Приоритет |
   Разделение: платные / бесплатные / партнёрские.

4. RISK REGISTER
   | Риск | Вероятность | Влияние | Митигация |
   Минимум 5 рисков, включая: «продукт не готов к дате», «низкий отклик аудитории», «конкурент запускает аналог».

