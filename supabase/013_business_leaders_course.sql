## Промпт 1: Полный SWOT-анализ с матрицей приоритетов

### Когда использовать

- Перед стратегической сессией или годовым планированием — чтобы прийти с готовой аналитикой, а не тратить время на мозговой штурм
- При выходе на новый рынок или запуске нового продукта — чтобы объективно оценить готовность компании
- После значимых изменений в отрасли (новый конкурент, регуляторные изменения, технологический сдвиг) — чтобы переоценить позиции

### Готовый промпт

```prompt
Ты — опытный стратегический консультант с 15-летним опытом работы
в McKinsey и BCG. Проведи глубокий SWOT-анализ компании на основе
предоставленных данных.

<company_data>
Название компании: {company_name}
Отрасль: {industry}
Основные продукты/услуги: {products}
Текущая позиция на рынке: {market_position}
Годовая выручка (порядок): {revenue_range}
Количество сотрудников: {team_size}
