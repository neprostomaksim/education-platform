## Промпт 2: Индивидуальный план развития (IDP)

### Когда использовать
- Вы проводите performance review и хотите перевести результаты оценки в конкретный план действий.
- Сотрудник хочет расти, но «не знает, что конкретно делать» — нужна дорожная карта.
- Вам нужно обосновать решение о повышении или обосновать, что именно сотрудник должен «подтянуть» для следующего уровня.

### Готовый промпт

```prompt
