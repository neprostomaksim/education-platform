2. **{competency_1-4}** — выберите 3-5 ключевых компетенций. Примеры: «управление конфликтами», «принятие решений в условиях неопределённости», «кросс-функциональное взаимодействие», «ориентация на результат».
3. **{team_context}** — опишите, с кем и как работает сотрудник: удалённая команда, быстрорастущий отдел, работа с ключевыми клиентами.
4. Если у вас несколько интервьюеров, распределите компетенции между ними — каждый оценивает 1-2 компетенции глубоко.

### Пример использования

