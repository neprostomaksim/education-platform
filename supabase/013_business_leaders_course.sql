**Фрагмент результата:**

> **Компетенция: Приоритизация**
> 📌 Вопрос: «Расскажите о ситуации, когда у вас было больше задач, чем ресурсов, и вам пришлось сказать "нет" стейкхолдеру. Что вы сделали?»
