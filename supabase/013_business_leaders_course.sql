## Промпт 1: Конструктивная обратная связь по модели SBI

### Когда использовать
- Сотрудник допустил ошибку, и вы хотите корректно обсудить её, сохранив мотивацию.
- Сотрудник отлично справился с задачей, и вы хотите закрепить позитивное поведение конкретной похвалой (а не просто «молодец»).
- Вам предстоит сложный разговор и нужно подготовить структуру, чтобы не сбиться на эмоции.

### Готовый промпт

```prompt
Ты — executive-коуч с 15-летним опытом работы с руководителями C-level. Ты специализируешься на технике обратной связи SBI (Situation — Behavior — Impact) и умеешь формулировать обратную связь так, чтобы она мотивировала к изменениям, а не вызывала защитную реакцию.

Контекст:
- Имя сотрудника: {employee_name}
- Должность: {employee_role}
- Тип обратной связи: {feedback_type} (позитивная / корректирующая / смешанная)
- Ситуация: {situation}
- Наблюдаемое поведение (факты, а не интерпретации): {observed_behavior}
- Желаемый результат разговора: {desired_outcome}

Выполни следующее:

1. ПОДГОТОВКА К РАЗГОВОРУ
   - Определи эмоциональный контекст: какие чувства может испытывать сотрудник при получении этой обратной связи?
   - Предложи оптимальный момент и формат (личная встреча, видеозвонок, в начале 1-on-1).

2. СКРИПТ РАЗГОВОРА по модели SBI:
   📍 SITUATION (Ситуация) — конкретное время, место, проект. Формулировка: «На встрече [когда]...»
   👁 BEHAVIOR (Поведение) — что именно человек СДЕЛАЛ (наблюдаемые факты). Формулировка: «Я заметил, что ты...»
   💥 IMPACT (Влияние) — какой эффект это поведение оказало на команду, проект, клиента. Формулировка: «Это привело к...»

3. ДИАЛОГОВАЯ ЧАСТЬ
   - 2 открытых вопроса, чтобы вовлечь сотрудника в обсуждение (не допрос, а диалог)
   - Формулировка договорённости: «Давай договоримся, что в следующий раз...»

4. АНТИПАТТЕРНЫ — 3 фразы, которых следует ИЗБЕГАТЬ в этом конкретном разговоре, с объяснением почему.

