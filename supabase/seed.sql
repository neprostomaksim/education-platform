'# Chain-of-Thought (CoT)

## Что это?

**Chain-of-Thought** — техника, при которой мы просим ИИ **рассуждать пошагово**, а не давать готовый ответ.

## Пример без CoT

```
Сколько будет 23 × 17?
→ 391 (часто ошибается)
```

## Пример с CoT

```
Сколько будет 23 × 17? Рассуждай пошагово.

→ 23 × 17
→ = 23 × 10 + 23 × 7  
→ = 230 + 161
→ = 391 ✅
```

## Когда использовать CoT?

- Математические задачи
- Логические рассуждения
- Сложный анализ данных
- Принятие решений

## Магическая фраза

Добавьте в конец промпта:

> *«Давай подумаем шаг за шагом»* (Let''s think step by step)

