## Промпт — это навык №1

**Промпт — это текстовая задача, которую вы ставите нейросети.** Всё, что вы пишете ИИ, — это промпт.

Аналогия (вспомните Занятие 1): **ИИ — блестящий стажёр.** Он прочитал полмира, но не знает ни вас, ни вашу задачу. Промпт — это **техническое задание** для стажёра. Скажете «сделай что-нибудь по маркетингу» — получите общую воду. Дадите чёткое ТЗ — получите работу уровня эксперта.

Главный закон: **мусор на входе — мусор на выходе.** Не ИИ «тупой» — просто задача поставлена расплывчато.

> 🧲 Почему это навык №1: один и тот же ChatGPT одному человеку пишет гениальные сценарии, а другому — банальщину. Модель одна. Разница — в промпте.

### 🛠 Практика 1. Почувствуйте разницу

Откройте любую нейросеть и отправьте два запроса подряд:

1. **Слабый:** `Напиши сценарий для Reels.`
2. **Сильный:** `Ты — сценарист коротких видео. Напиши сценарий Reels на 30 секунд для [ТВОЯ НИША] на тему [ТЕМА]. Аудитория: [КОМУ]. Начни с хука, который удержит за 3 секунды. В конце — повод сохранить. Формат: по репликам.`

**🎯 База:** отправьте оба, сравните ответы, выпишите 3 отличия — чем второй лучше.
**🚀 Бонус:** перепишите второй промпт ещё сильнее и добейтесь результата, который не стыдно снять.

> 🔓 **Доступ из РБ:** любая нейросеть (ChatGPT, Gemini, DeepSeek).

## Формула сильного промпта

Чтобы не выдумывать промпт каждый раз, есть простая формула из 5 блоков. Не обязательно все сразу — но чем больше, тем точнее результат.

**Роль → Задача → Контекст → Формат → Примеры/Ограничения.**

| Блок | Что это | Пример |
|---|---|---|
| **Роль** | кем должен быть ИИ | «Ты — сценарист Reels с опытом в бьюти-нише» |
| **Задача** | что сделать, одним глаголом | «Напиши 5 сценариев» |
| **Контекст** | про вас, ЦА, цель — тут ваш Паспорт бренда | «Моя ниша…, аудитория…, боли…» |
| **Формат** | как выдать результат | «Список, по каждому: хук, тело, финал» |
| **Примеры/Ограничения** | образец или рамки | «Без "всем привет". Хук — в боль. До 150 слов» |

Сравните:

| ❌ Слабый промпт | ✅ Сильный промпт |
|---|---|
| «Придумай идеи для блога» | «Ты — контент-стратег. Придумай 10 идей Reels для [ниша]. Аудитория и боли: […]. Каждая идея: тема + хук за 3 сек + формат. Одна ниша, без скачков.» |
| «Напиши пост» | «Ты — копирайтер. Напиши подпись к посту про [тема] для [ЦА] в тоне [tone of voice]. Первая строка — цепляющая. В конце — вопрос для комментариев.» |

> 💡 **Контекст = ваш Паспорт бренда.** Именно поэтому мы собирали его на Занятии 2: он закрывает самый важный блок формулы одним куском текста. Дальше вы просто вставляете Паспорт в промпт — и ИИ сразу «в теме».

