## Промпт — это навык №1

**Промпт — это текстовая задача, которую вы ставите нейросети.** Всё, что вы пишете ИИ, — это промпт.

Аналогия (вспомните Занятие 1): **ИИ — блестящий стажёр.** Он прочитал полмира, но не знает ни вас, ни вашу задачу. Промпт — это **техническое задание** для стажёра. Скажете «сделай что-нибудь по маркетингу» — получите общую воду. Дадите чёткое ТЗ — получите работу уровня эксперта.

Важно понять главное: современный ИИ уже редко выдаёт откровенный мусор — он выдаёт **среднее, безликое, «как у всех».** А среднее в ленте не выстреливает (вспомните Занятие 1: побеждает то, что цепляет и попадает в конкретного зрителя). Задача сильного промпта — не «спасти» ИИ, а **превратить общий ответ в готовый под вас**: вашу нишу, вашу аудиторию, ваш голос.

> 🧲 Почему это навык №1: один и тот же ChatGPT одному человеку пишет сценарии, которые хочется снять, а другому — гладкую банальщину, которую постить стыдно. Модель одна. Разница — в промпте.

### 🛠 Практика 1. «Средний ответ» против «вашего»

Современный ИИ ответит на оба запроса — и оба будут «нормальными». Ваша задача — увидеть, где ответ **безликий и общий**, а где **готов к публикации именно в вашем блоге.**

Отправьте два запроса:

1. **Слабый:** `Напиши сценарий для Reels.`
2. **Сильный:** `Ты — сценарист коротких видео. Напиши сценарий Reels на 30 секунд для [ТВОЯ НИША] на тему [ТЕМА]. Аудитория: [КОМУ]. Начни с хука, который удержит за 3 секунды. В конце — повод сохранить. Формат: по репликам.`

Теперь прогоните **каждый ответ по чек-листу** и посчитайте галочки:

- [ ] Это про мою нишу, а не «вообще»?
- [ ] Попадает в боль или желание моей аудитории?
- [ ] Звучит моим голосом?
- [ ] Есть хук, который удержит за 3 секунды?
- [ ] Можно выложить без переделки?

Обычно слабый набирает 1–2 галочки (ответ «средний, для всех»), сильный — 4–5. **Вот в чём теперь разница: не «работает / не работает», а «безликое / готовое под вас».**

**🎯 База:** прогоните оба ответа по чек-листу, сравните число галочек.
**🚀 Бонус:** отправьте **слабый** промпт 3 раза подряд — увидите, как ответы «гуляют» по теме и качеству. Сильный промпт держит цель каждый раз. **На потоке вам нужна не удача, а стабильность.**

> 🔓 **Доступ из РБ:** любая нейросеть (ChatGPT, Gemini, DeepSeek).

## Формула сильного промпта

Чтобы не выдумывать промпт каждый раз, есть простая формула из 5 блоков. Не обязательно все сразу — но чем больше, тем точнее результат.

**Роль → Задача → Контекст → Формат → Примеры/Ограничения.**

| Блок | Что это | Пример |
|---|---|---|
| **Роль** | кем должен быть ИИ | «Ты — сценарист Reels с опытом в бьюти-нише» |
| **Задача** | что сделать, одним глаголом | «Напиши 5 сценариев» |
| **Контекст** | про вас, ЦА, цель — тут ваш Паспорт бренда | «Моя ниша…, аудитория…, боли…» |
| **Формат** | как выдать результат | «Список, по каждому: хук, тело, финал» |
| **Примеры/Ограничения** | образец или рамки | «Без "всем привет". Хук — в боль. До 150 слов» |

Сравните:

| ❌ Слабый промпт | ✅ Сильный промпт |
|---|---|
| «Придумай идеи для блога» | «Ты — контент-стратег. Придумай 10 идей Reels для [ниша]. Аудитория и боли: […]. Каждая идея: тема + хук за 3 сек + формат. Одна ниша, без скачков.» |
| «Напиши пост» | «Ты — копирайтер. Напиши подпись к посту про [тема] для [ЦА] в тоне [tone of voice]. Первая строка — цепляющая. В конце — вопрос для комментариев.» |

### Разбираем три главных элемента

Три блока формулы решают почти всё. Разберём их чуть глубже.

**🎭 Роль — это кто + опыт + стиль.** Не просто «ты — сценарист», а с деталями. Роль сужает «память» ИИ и включает режим эксперта.
- ❌ «Ты — сценарист.»
- ✅ «Ты — сценарист коротких видео с опытом в бьюти-нише, пишешь живо и без штампов, удерживаешь внимание с первой секунды.»

**📋 Контекст — собирайте «от результата назад».** Главная ошибка — вываливать всё подряд, промпт перегружается и сбивает ИИ. Спросите себя: *«что ИИ обязан знать, чтобы выдать нужный результат без уточнений?»* — и дайте только это. Для контента это ниша, аудитория с её болями и ваш голос.
> 💡 Это и есть ваш **Паспорт бренда** из Занятия 2 — он одним куском закрывает весь блок «Контекст». Вставили Паспорт — контекст готов.

**🎯 Задача — сильные глаголы + одна команда + рамки.**
- **Сильные глаголы:** не «помоги с постом», а «напиши», «сократи», «придумай 10».
- **Одна команда за раз:** не просите в одном промпте сразу сценарий, идеи для монтажа и хэштеги — всё выйдет поверхностно.
- **Жёсткие рамки:** «до 150 слов», «ровно 5 идей», «выдай таблицей».

