## От промпта к помощнику: Gem-боты и GPT

Пока вы пишете промпт заново каждый раз. Но лучший промпт хочется **сохранить один раз и пользоваться всегда.** Для этого есть **боты-помощники**:

- **Gemini Gem** — сохранённый бот в Gemini (бесплатно, нужен только Google-аккаунт).
- **Custom GPT** — то же в ChatGPT (нужна платная подписка Plus).
- **Проект** в ChatGPT/Claude — папка с общими инструкциями для серии чатов.

Суть одна: вы **один раз** кладёте внутрь бота **системный промпт + свой Паспорт бренда**, даёте ему имя — и дальше просто пишете «сделай 5 сценариев про X», а он уже знает, кто вы, для кого и каким голосом.

Аналогия: разовый промпт — это **объяснять задачу новому фрилансеру каждый раз.** Бот — это **штатный сотрудник, который уже знает ваш бренд.**

### Как создать Gem-бота: пошагово

1. Откройте Gemini (`gemini.google.com`), войдите в Google-аккаунт.
2. Найдите раздел управления Gem-ботами и создайте новый («New Gem» / «Создать Gem»).
3. Дайте боту имя, например «Сценарист Reels».
4. В поле инструкций вставьте системный промпт помощника (из главы «Собираем помощников») и **вместо `[ПАСПОРТ БРЕНДА]` вставьте свой Паспорт** из Занятия 2.
5. Сохраните. Теперь бот всегда под рукой — пишете задачу, получаете результат в своём стиле.

`[СКРИН: создание нового Gem в Gemini — поля имени и инструкций]`

> 🔓 **Доступ из РБ.** Gemini и ChatGPT напрямую не открываются — нужен VPN и вход через Google. **Запасной вариант без ботов:** сохраните системные промпты помощников в заметках телефона и вставляйте их перед задачей в любую доступную нейросеть (DeepSeek работает из РБ напрямую). Результат тот же — просто контекст вставляете руками. На занятии настраиваем вживую; не получилось — работаем в паре или на общем экране преподавателя.

