## Контент-план: неделя контента за 20 минут

Даже сильные ролики не работают без **системы**. Контент-план отвечает на вопрос «что снимать и когда», чтобы не выгорать и не молчать неделями. Помните правило из Занятия 1: **одна ниша + регулярность** важнее вдохновения.

Простая модель — **3 типа контента**:

| Тип | Зачем | Пример |
|---|---|---|
| **Польза / экспертиза** | показать, что вы разбираетесь | «3 ошибки в …» |
| **Личное / вовлечение** | доверие, знакомство с вами | «как я пришёл в профессию» |
| **Мягкая продажа** | ненавязчиво про продукт/услугу | «как я помог клиенту с …» |

Рабочая пропорция — примерно **3 : 1 : 1** (на каждые 3 полезных ролика — 1 личный и 1 продающий).

### Агент 5. Контент-план

**Что делает:** из Паспорта бренда собирает готовый план на неделю/две по дням.

```prompt
[ПАСПОРТ БРЕНДА]

Ты — мой контент-продюсер. Составь контент-план на основе Паспорта бренда.

Используй 3 типа контента в пропорции примерно 3 : 1 : 1:
- Польза/экспертиза (основа)
- Личное/вовлечение (доверие)
- Мягкая продажа (ненавязчиво про мой продукт/услугу)

Задача: план на [7 или 14] дней. Для каждого дня выдай:
- Тема ролика
- Тип контента (польза / личное / продажа)
- Хук за 3 секунды
- Формат (говорящая голова / процесс / карусель / текст на экране)

Всё — одна ниша, мой голос и аудитория из Паспорта. Выдай таблицей.
```

**Как пользоваться:** получите план → лучшие темы сразу прогоните через Сценариста Reels. За 20 минут — неделя контента.

> 🔓 **Доступ из РБ:** любая доступная нейросеть.

