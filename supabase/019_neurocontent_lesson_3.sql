## Приёмы, которые усиливают промпт

Пять приёмов, которые превращают нормальный промпт в отличный:

1. **Примеры (few-shot).** Покажите ИИ 1–2 образца «как надо» — он подхватит стиль. *«Вот пример хорошего хука: … Сделай 5 таких».*
2. **«Думай по шагам».** Для сложных задач попросите рассуждать поэтапно — меньше ошибок. *«Сначала опиши аудиторию, потом придумай хук, потом сценарий».*
3. **Диалог, а не один выстрел.** Не ждите идеала сразу. Уточняйте: *«слишком формально, добавь юмора»*, *«сократи вдвое»*. ИИ помнит контекст беседы.
4. **Переменные-заглушки.** Оставляйте `[НИША]`, `[ТЕМА]`, `[ПАСПОРТ БРЕНДА]` — так один промпт переиспользуется под любую задачу.
5. **Негативные инструкции.** Скажите, чего НЕ делать: *«без канцелярита»*, *«не начинай с приветствия»*, *«никакой воды»*.

| ❌ Без приёмов | ✅ С приёмами |
|---|---|
| «Напиши 5 хуков про сон» | «Вот пример сильного хука: "Ты не устал — ты недоспал 40 минут". Сделай 5 таких про сон для [ЦА]. Без "всем привет", коротко, бей в боль.» |

