## Что важно запомнить

- 🎬 **Решает удержание, а не длина** — хук в первые 3 секунды, динамика с первого кадра.
- 🎥 **Ролик = набор коротких кадров (~4–8 сек)** — сначала концепция и раскадровка, потом генерация.
- 🖼️ **Старт-кадр под оживление ≠ красивое фото:** чисто, резко, место для движения, кадр = начало действия.
- ▶️ **I2V — из кадра** (точный вид, консистентный герой); **T2V — из текста** (быстро, вид не критичен).
- ✏️ **Omni-модели (Veo, Kling) правят готовое видео словами** — замена объекта/фона, свет и атмосфера, убрать/добавить элемент, стиль — сохраняя остальной кадр (как Nano Banana для картинок). Правило: назови, **что менять И что оставить.** Плюс видео со звуком/речью (липсинк), референсы героя (Ingredients/Elements), старт/финальный кадр и продление сцены.
