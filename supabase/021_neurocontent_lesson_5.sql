## Оживляем картинку: image-to-video

**Image-to-video (I2V):** даём модели старт-кадр + **промпт движения** — она анимирует. Что писать:

- **Движение субъекта** — что и как делает (плавно/резко).
- **Камера** — статика / медленный наезд / облёт (камера обходит субъект) / панорама (поворот на месте) / тревеллинг (едет вслед за субъектом).
- **Темп и атмосфера.**
- **Что сохранить неизменным** — лицо, стиль, композицию.

Правило удержания: движение должно начинаться **сразу**, без «пустого» первого кадра.

| ❌ Слабый промпт движения | ✅ Сильный |
|---|---|
| «сделай красиво, пусть двигается» | «бариста делает шаг к камере, лёгкий наезд; пар над чашкой поднимается; лицо и одежда без изменений; ~5 сек» |

> 🔓 **Доступ из РБ.** Основной инструмент — **Veo** (в Gemini): нужен VPN, Google-аккаунт и подписка (Veo — платный). Запасной вариант — **Kling** (из РБ доступнее, есть бесплатные кредиты) — промпт движения туда подходит тот же. На занятии — демо на общем экране и работа в парах для тех, у кого нет доступа.

