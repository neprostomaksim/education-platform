## Omni-модели: видео со звуком (Veo 3.1 Omni, Kling Omni)

Раньше видео и озвучку делали **отдельно**. **Omni-модели** (например, **Veo 3.1 Omni**, **Kling Omni**) генерят всё за **один проход** — видео **со звуком**: эмбиент, эффекты и даже **синхронную речь** персонажа.

Как промптить звук:

- **Реплики** — дословно в кавычках: *персонаж говорит: «...»*. Модель сама сведёт губы с речью (липсинк).
- **Фон и эффекты** — опишите словами: шум города, музыка, шаги, дождь.
- Не перегружайте: 1 реплика или 1–2 звуковых слоя на короткий кадр.

Когда лучше Omni: когда нужен **звук или речь «из коробки»**, без отдельной озвучки. *(Полноценные говорящие аватары и клон голоса — на Занятии 6.)*

