## Первые кадры для оживления

Многие клипы делаются **из стартового кадра** (image-to-video): сначала картинка, потом её оживляем. И такой кадр готовится **не как «красивое фото»**, а по своим правилам — чтобы модели было удобно его анимировать:

1. **Чисто и резко**, без смазов — движение добавит видеомодель, а смаз на старте она поймёт криво.
2. **Оставьте место для движения:** если субъект/камера пойдут в сторону — не обрезайте по краю, дайте «воздух».
3. **Субъект чётко отделён от фона**, сцена не перегружена.
4. **Кадр = начало действия** («рука только пошла вперёд»), а не финал.
5. **Свет нейтральный**, без экстремального контрового света (жёсткого света сзади — он мерцает при анимации).
6. **Формат под видео** — 9:16 (вертикаль) или 16:9.

| ❌ Плохо (кадр «поплывёт») | ✅ Хорошо (годится под оживление) |
|---|---|
| Резкое движение и смаз уже в кадре, субъект обрезан у края, перегруженный фон | Субъект в начале действия, чётко отделён от фона, есть «воздух» в сторону движения |

Старт-кадры генерируем инструментами из Занятия 4 — через **арт-директора** (у него для этого есть тип «стартовый кадр для видео»).

