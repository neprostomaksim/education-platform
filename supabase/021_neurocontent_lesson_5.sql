## Text-to-video: видео из текста

**Text-to-video (T2V):** описываем сцену **текстом** — модель генерит клип **без старт-кадра**. Промпт похож на визуальный из Занятия 4, плюс движение: субъект + действие + окружение + камера + стиль.

Готовый образец:

```prompt
Кинематографичный клип: [СУБЪЕКТ] [ЧТО ДЕЛАЕТ] в [ОКРУЖЕНИЕ].
Камера: [медленный наезд / панорама / облёт]. Свет: [мягкий дневной / золотой час].
Стиль: фотореализм, [настроение]. Движение начинается сразу. Длительность ~5 секунд.
```

`[СКРИН: Veo — поле text-to-video]`

Когда что выбрать:

| Image-to-video (из кадра) | Text-to-video (из текста) |
|---|---|
| нужен **точный вид** кадра или **консистентный герой** (старт-кадр из фотосессии) | нужен **быстрый** клип, точный вид не критичен |
| продукт/лицо должны быть узнаваемы | абстрактные, динамичные, «атмосферные» сцены |
| больше контроля | быстрее и проще |

