## Omni-модели: редактируем готовое видео словами (Veo, Kling)

До сих пор мы видео **генерировали**. Но что делать, если ролик почти идеальный — а фон не тот, куртка не того цвета или в кадр залез прохожий? Раньше — перегенерировать целиком (и получить *другой* ролик) или лезть в тяжёлый видеоредактор.

**Omni-модели решают это иначе: они правят уже готовое видео обычным текстом** — как Nano Banana правит картинку из Занятия 4, только для видео. Вы говорите, *что* поменять и *что оставить*, а модель меняет точечно, **сохраняя остальной кадр** — движение камеры, тени, свет и всё, что вы не трогали. Это и есть их главная суперсила (нативный звук у них тоже есть — но об этом ниже).

Что умеют Omni при правке готового ролика:

| Правка | Пример команды | Зачем в контенте |
|---|---|---|
| **Замена объекта/персонажа** | «замени куртку на джинсовую», «поставь на стол наш продукт» | вставить свой товар в готовый ролик без пересъёмки |
| **Смена фона** | «перенеси сцену в лофт с окнами» | одна съёмка → разные локации |
| **Свет и атмосфера** | «сделай из дня закат», «добавь дождь» | сменить настроение, не трогая героя |
| **Убрать/добавить элемент** | «убери прохожих на фоне», «добавь пар от кофе» | чистка кадра и мелкие акценты |
| **Стиль** (style transfer) | «переведи в акварель / в стиль 3D-мультика» | быстрый ремикс под другую подачу |
| **Правка по референсу** | текст + картинка-образец, что именно вставить | точное попадание вместо «похоже, но не то» |

Главное правило — **то же, что у Nano Banana: называйте, что менять И что сохранить.**
- ❌ «сделай покрасивее»
- ✅ «замени фон на морской берег на закате, **персонажа, его позу и движение камеры оставь как есть**»

А ещё Omni генерят видео **со звуком за один проход** и дают режиссёрские инструменты:

| Возможность | Что это | Зачем в контенте |
|---|---|---|
| **Нативный звук** | речь, эффекты, музыка рождаются вместе с картинкой (не постобработка) | готовый ролик со звуком без отдельной озвучки |
| **Липсинк / речь** | губы синхронны с репликой (у Kling — многоязычно) | говорящий герой прямо в камеру |
| **Референсы героя** — Veo «Ingredients» (до 3) / Kling «Elements» (до 4) | прикладываете лицо, одежду, фон как образцы | **один и тот же персонаж** в разных кадрах |
| **Старт- и финальный кадр** | задаёте первый и последний кадр — модель строит переход | точный контроль движения и склеек |
| **Продление сцены** (Veo Scene Extension) | достраивает продолжение из последней секунды | ролик длиннее без разрыва |
| **Качество** | Veo — вплоть до 4K | чёткая картинка под ленту |

Как промптить звук: **реплики** — дословно в кавычках (*говорит: «...»*, модель сведёт губы); **фон и эффекты** — словами (шум города, дождь, шаги); не перегружайте — 1 реплика или 1–2 звуковых слоя на кадр.

Когда брать Omni: нужно **поправить готовый ролик, не пересобирая его**, либо получить **звук/консистентного героя/длинный кадр** без отдельного монтажа. *(Полноценные говорящие аватары и клон голоса — на Занятии 6.)*

