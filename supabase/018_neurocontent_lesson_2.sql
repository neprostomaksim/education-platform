## Позиционирование простыми словами

**Позиционирование — это место, которое вы занимаете в голове зрителя одной фразой.** Когда человек думает «кто объяснит мне про ремонт без обмана» или «у кого учиться макияжу после 40» — вы хотите быть тем именем, которое всплывает первым.

Аналогия: **позиционирование — это вывеска над магазином.** Если на вывеске «Пекарня» — голодный человек заходит. Если «Продаём всё» — мимо проходят все: непонятно, что внутри и зачем. Блог работает так же.

### Почему блог «обо всём» убивает охваты

Это не вкусовщина, а математика алгоритма. Вспомните правила из Занятия 1:

- Соцсеть в 2026 показывает ролик **сначала вашим же подписчикам** (модель «follower-first») и смотрит на реакцию.
- Чтобы показать вас новым людям, алгоритму нужно понять, **кому именно вы интересны**. Он определяет это по теме ваших роликов.

Если сегодня вы про путешествия, завтра про крипту, послезавтра про похудение — алгоритм **не понимает, кому вас рекомендовать**, и перестаёт расширять охват. **Разброс по 3+ темам режет охват примерно на 45%.**

> 🧲 **Одна ниша — это не клетка, а фокус.** Внутри ниши тем сколько угодно: «ремонт» — это и ошибки подрядчиков, и сколько стоит кухня, и лайфхаки с плиткой. Нельзя — прыгать между *разными* нишами в одном блоге.

| ❌ Блог «обо всём» | ✅ Блог про одно |
|---|---|
| Алгоритм не понимает, кому показывать → охват падает | Алгоритм быстро находит «вашу» аудиторию → охват растёт волнами |
| Подписчик не знает, чего от вас ждать | Подписчик знает, зачем подписан, и ждёт ваши ролики |
| «Просто ещё один блогер» | «Тот самый человек по теме X» |

### Позиционирование в одну фразу: плохо / хорошо

Хорошее позиционирование умещается в одно предложение и отвечает на три вопроса: **кто вы → для кого → какую пользу даёте.**

| ❌ Плохо (размыто) | ✅ Хорошо (конкретно) |
|---|---|
| «Веду блог про красоту и жизнь» | «Учу женщин после 40 делать макияж за 10 минут, который не старит» |
| «Рассказываю про бизнес и саморазвитие» | «Помогаю мастерам маникюра поднять цены и перестать работать за еду» |
| «Эксперт по психологии» | «Объясняю родителям подростков, как говорить с ребёнком без скандалов» |

`[СКРИН: профиль блогера, где из шапки сразу ясно, кто он и для кого]`

