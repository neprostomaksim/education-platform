## Архетипы бренда: все 12

Вы знаете, кто вы и для кого. Теперь — **каким голосом** говорить. За это отвечает архетип.

**Архетип — это узнаваемый характер вашего бренда.** Аналогия: **как герой в кино.** Мы любим фильмы не за список спецэффектов, а за характеры: мудрый наставник, дерзкий бунтарь, добрый защитник. Люди подписываются не на «полезный контент вообще», а на **характер**, который им близок.

Идею 12 архетипов придумал психолог **Карл Юнг**, а для брендов её разложили **Маргарет Марк и Кэрол Пирсон**. Суть: есть 12 устойчивых характеров, понятных любому человеку на планете. Ваш блог тоже несёт один из них — осознанно или нет. Лучше выбрать осознанно.

> 💡 У большинства людей **один ведущий архетип + один поддерживающий**. Не пытайтесь быть всеми сразу — это снова блог «обо всём», только на уровне характера.

12 архетипов делятся на 4 группы по тому, чего человек хочет от жизни.

### Группа 1. Свобода и познание

- **Невинный (Innocent).** Несёт добро, простоту и оптимизм; верит, что жизнь может быть лёгкой и честной. *Бренды:* Dove, Coca-Cola. *Блогер:* «эко-мама» про простую и чистую жизнь. *Тон:* добрый, светлый, без негатива. *Темы:* простые радости, честность, «как сделать жизнь проще». *Образ:* светлые чистые тона (белый, нежно-голубой, бежевый), простая опрятная одежда, мягкий свет, лёгкая улыбка.
- **Мудрец (Sage).** Ищет истину и делится знанием, объясняет сложное. *Бренды:* Google, National Geographic, BBC. *Блогер:* эксперт-аналитик, «разбираю по полочкам». *Тон:* спокойный, вдумчивый, доказательный. *Темы:* разборы, факты, «почему это работает», мифы и правда. *Образ:* глубокие спокойные тона (тёмно-синий, графит, бордо), аккуратный «профессорский» стиль (рубашка, очки), нейтральный фон.
- **Искатель (Explorer).** Ценит свободу и новый опыт, не терпит рамок. *Бренды:* Jeep, The North Face, Patagonia. *Блогер:* тревел-блогер-одиночка, «жизнь вне офиса». *Тон:* свободный, честный, немного дерзкий. *Темы:* путешествия, новый опыт, независимость, выход из зоны комфорта. *Образ:* природные оттенки (хаки, терракота, песочный), функциональная одежда (деним, куртки), съёмка на локациях и в движении.

### Группа 2. Оставить след: мастерство и риск

- **Герой (Hero).** Доказывает силу через действие и преодоление, вдохновляет «соберись и сделай». *Бренды:* Nike, Adidas. *Блогер:* фитнес-тренер «без оправданий», предприниматель про дисциплину. *Тон:* мотивирующий, прямой, энергичный. *Темы:* преодоление, результаты, дисциплина, «до и после». *Образ:* контрастные энергичные тона (чёрный, красный, стальной), спортивный или собранный деловой вид, динамичный кадр.
- **Бунтарь (Outlaw).** Ломает правила и устои, идёт против системы. *Бренды:* Harley-Davidson, Diesel. *Блогер:* «говорю то, о чём все молчат», разоблачитель индустрии. *Тон:* дерзкий, провокационный, честный до резкости. *Темы:* разоблачения, «как не надо», свобода от навязанного. *Образ:* жёсткая палитра (чёрный, тёмно-серый, кислотный акцент), кожа/деним/тёмное, резкий контраст и свет.
- **Маг (Magician).** Превращает невозможное в возможное, даёт трансформацию и «вау». *Бренды:* Apple, Disney, Tesla. *Блогер:* коуч-трансформатор, нейро-эксперт «покажу магию». *Тон:* вдохновляющий, немного загадочный, обещающий результат. *Темы:* трансформации, инсайты, «было → стало», новые технологии. *Образ:* глубокие «магические» тона (фиолетовый, тёмно-синий, золотой акцент), стильная тёмная одежда, эффектный свет.

### Группа 3. Принадлежность и связь

- **Славный малый (Everyman).** «Я такой же, как ты», без пафоса и понтов. *Бренды:* IKEA, Lay's. *Блогер:* «обычная жизнь как есть», лайфхаки для всех. *Тон:* простой, дружелюбный, на равных. *Темы:* быт, доступные решения, «без фильтров». *Образ:* тёплые повседневные тона (серый, джинсовый, тёплый бежевый), обычная удобная одежда, домашний/бытовой фон.
- **Любовник (Lover).** Про красоту, чувства, эстетику и удовольствие. *Бренды:* Chanel, Magnum. *Блогер:* про эстетику, отношения, вкус, красоту. *Тон:* чувственный, тёплый, эстетичный. *Темы:* красота, отношения, удовольствие, забота о себе. *Образ:* мягкие чувственные тона (винный, пудровый, золотой), эстетичная стильная одежда, тёплый свет, красивый кадр.
