## Сильные стороны: ИИ-интервью по себе

Первый кирпич фундамента — **вы сами.** Что вы знаете и умеете лучше других, какой у вас опыт, через что вы прошли. Без этого блог получается «как у всех».

Проблема в том, что **свои сильные стороны человек почти не видит** — то, что даётся вам легко, кажется «ну это же все умеют». Аналогия: **рыба не замечает воду.** Ваш реальный опыт — это вода, в которой вы живёте, и со стороны его видно гораздо лучше.

Поэтому мы не будем мучительно вспоминать всё в одиночку. Мы посадим напротив **ИИ-интервьюера**: он задаёт вопросы по одному, цепляется за ответы и вытаскивает то, что вы сами считаете обычным, — а на деле это ваша экспертиза.

### Как провести ИИ-интервью: пошагово

1. Откройте любую нейросеть (ChatGPT, Gemini или DeepSeek как запасной).
2. Скопируйте промпт «ИИ-интервьюер сильных сторон» ниже и вставьте его.
3. Если знаете нишу — впишите её в заглушку. Не знаете — так и напишите «пока не знаю», агент поможет.
4. **Отвечайте на вопросы по одному, честно и подробно.** Не отписывайтесь одним словом — чем больше деталей и историй, тем сильнее результат.
5. Дойдите до конца (10–15 вопросов). В финале агент соберёт блок **«Сильные стороны»**.
6. **Скопируйте этот блок в отдельный файл** — он понадобится в следующих главах.

### 🛠 Практика: Агент 1. ИИ-интервьюер сильных сторон

**Что делает:** через живой диалог по одному вопросу вытаскивает ваш опыт, экспертизу и сильные стороны и собирает их в готовый блок.

