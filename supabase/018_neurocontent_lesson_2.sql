## Файл-фундамент и «Паспорт бренда»

Вы прошли весь путь и собрали 6 кирпичей. Теперь сложим их в **один файл-фундамент** — это «ДНК вашего блога».

Зачем он нужен дальше. На следующих занятиях мы будем собирать ИИ-помощников: сценариста Reels, копирайтера постов, генератора идей. Каждому из них нужно знать, **кто вы и для кого** — иначе он выдаёт безликий текст «для всех». Объяснять это заново в каждом промпте — долго.

Аналогия: **файл-фундамент — это паспорт вашего блога.** Показали один раз — и любой ИИ-помощник сразу знает, с кем имеет дело.

### Что входит в файл-фундамент

> ⚠️ **Главное:** файл-фундамент — это **не новый текст, который вы пишете с нуля.** Это склейка готовых ответов агентов 1–4. Вы только копируете блоки между агентами, а финальную сборку делает **Агент 5**: он чистит, убирает повторы и раскладывает всё по 6 полям. Печатать руками ничего не нужно.

Только то, что **напрямую влияет на контент и сценарии** — ничего лишнего:

| Поле | Что это | Откуда взяли |
|---|---|---|
| **1. Кто я** | опыт, экспертиза, сильные стороны | Агент 1 (интервью) |
| **2. Ниша и темы** | одна ниша, о чём блог | Агент 1 + глава 1 |
| **3. ЦА: боли и желания** | для кого и что у них болит | Агент 2 |
| **4. Архетип и образ** | характер, голос, визуал для видео (цвета, одежда) | Агент 3 |
| **5. Позиционирование** | одна фраза «кто/кому/чем полезен» | Агент 4 |
| **6. Tone of voice** | как звучу / как не звучу | Агент 4 |

### Паспорт бренда — сжатая версия для ИИ

Полный файл-фундамент — для вас, перечитывать. Но вставлять его целиком в каждый промпт громоздко. Поэтому из него выжимаем **«Паспорт бренда»** — те же 6 пунктов, но плотно, на 6–10 строк. Именно его вы будете вставлять в промпты следующих занятий вместо заглушки:

```prompt
