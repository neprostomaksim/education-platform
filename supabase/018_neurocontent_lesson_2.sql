## Собираем позиционирование

Теперь у вас три ингредиента: **сильные стороны** (кто вы), **портрет ЦА** (для кого) и **архетип** (каким голосом). Позиционирование — это **рецепт, который соединяет их в одно блюдо.**

Рабочая формула фразы позиционирования:

> **Помогаю [КОМУ] достичь [ЧЕГО] через [ЧЕМ Я ОТЛИЧАЮСЬ].**

- *Кому* — берём из портрета ЦА.
- *Чего* — их главное желание.
- *Чем отличаюсь* — ваши сильные стороны + характер архетипа.

Отсюда же рождается **tone of voice (голос бренда)** — как именно вы звучите: какие слова используете, а какие нет. Голос диктует архетип: Заботливый говорит мягко и поддерживающе, Бунтарь — резко и прямо. Один и тот же совет в разных голосах звучит по-разному — и притягивает разных людей.

