## Целевая аудитория без воды

Когда новичка спрашивают «кто твоя аудитория?», он отвечает: **«Женщины 25–45, город, средний доход».** Это **демография** — и для контента она почти бесполезна.

Аналогия: вы готовите ужин для гостя. Знание «ему 30, живёт в Минске» **не помогает** выбрать блюдо. А «он не ест мясо и обожает острое» — помогает сразу. **Боли и желания — это «что человек не ест и что обожает». Демография — это просто его паспорт.**

**Вашему ролику важно одно: попасть в боль или желание зрителя в первые 3 секунды.** Поэтому портрет ЦА мы собираем не через возраст, а через то, **что у человека болит и чего он хочет.**

- **Боль** — то, что бесит, пугает или мешает прямо сейчас. Из боли рождается **хук**.
- **Желание** — картинка «как должно быть», к которой человек стремится. Из желания рождается **обещание ролика**.

Хороший ролик берёт зрителя из точки «боль» и показывает дорогу в точку «желание».

| ❌ Портрет через демографию | ✅ Портрет через боли и желания |
|---|---|
| «Женщины 30–45, есть дети» | **Боль:** «Времени на себя нет, в зеркале — уставшее лицо» |
| «Доход средний и выше» | **Желание:** «Выглядеть свежо, потратив 10 минут утром» |
| «Живут в крупных городах» | **Боль:** «Купила кучу косметики, а пользоваться не умею» |
| «Интересуются красотой» | **Желание:** «Простая система, а не 30 баночек» |

Под каждую строку справа **уже можно снять ролик**. Под левую — нельзя.

> ⚠️ **Три частые ошибки:** ① «моя аудитория — все» (значит, ни для кого); ② придумывать боли из головы вместо реальных слов людей (комментарии, директ, отзывы); ③ путать свою боль («химия состава крема») с болью зрителя («выгляжу старше своих лет»).

