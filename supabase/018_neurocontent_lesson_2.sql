> 🔌 **Слот для подключения.** Контент-агенты Занятий 3–8 (сценарист, копирайтер и др.) будут начинаться со строки `[ПАСПОРТ БРЕНДА]`. Вы вставляете туда этот блок — и помощник сразу пишет вашим голосом, для вашей аудитории. Соберёте Паспорт один раз — пользуетесь весь курс.

