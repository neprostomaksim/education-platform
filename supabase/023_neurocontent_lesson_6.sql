## Модели аватара: фото или видео

HeyGen даёт три разных способа получить говорящего «себя» — и они не взаимозаменяемы:

| Модель | Что нужно | Верификация | Результат |
|---|---|---|---|
| **Photo Avatar** | 1 фото (для лучшего качества — 10–15) | Не требуется | Говорящий портрет: губы синхронны с текстом, лёгкая мимика |
