## Пишем сценарий для аватара

Аватар говорит ровно то, что вы ему дадите — а текст «для чтения» и текст «для произнесения на камеру» звучат по-разному. За сценарием далеко ходить не нужно — берём его тем же путём, что и раньше в курсе:

- **Путь 1 — из чужого залетевшего видео.** Как в Занятии 1: находите ролик, скачиваете, транскрибируете и **уникализируете** — получаете авторский текст на основе рабочей структуры.
- **Путь 2 — с нуля через своего сценариста.** Как в Занятии 3: используете личного помощника с вашим **Паспортом бренда** (Занятие 2) — он уже знает вашу нишу, аудиторию и голос.

В обоих случаях на выходе — обычный сценарий видео, а его ещё нужно **адаптировать под говорящего аватара** (приём ниже). Оба агента продублированы здесь же для скорости — это те же помощники, что и в Занятиях 1 и 3.

