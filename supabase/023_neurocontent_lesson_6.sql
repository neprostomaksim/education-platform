## Цифровой двойник и верификация: Digital Twin

Digital Twin — не просто «фото, которое говорит», а полноценная видео-копия: своя мимика, жесты и голос, взятые из настоящей записи. Именно поэтому HeyGen требует **доказательство согласия**, прежде чем оживлять видео реального человека.

**Как проходит консент-верификация:**

1. Перед созданием Digital Twin HeyGen просит записать короткое **consent-видео прямо с камеры** (не загрузка готового файла).
2. На камеру нужно произнести согласие: «я создаю аватар себя» (или «у меня есть право создать аватар этого человека»).
3. В конце ролика показывается **4-буквенный код** — его нужно прочитать вслух. Так система проверяет, что видео живое, а не заранее записанное.
4. Если аватар делают **для другого человека**, тот человек проходит согласие сам — сканирует QR-код и записывает видео со своего телефона, физически быть рядом не обязательно.

| ❌ Почему отклоняют | ✅ Как пройти с первого раза |
|---|---|
| Текст согласия прочитан невнятно, лицо уходит из кадра, плохой свет, звук рассинхронизирован, использовано уже сгенерированное видео | Читаете текст чётко и полностью в кадре, ровный свет, стабильная камера, записываете реальное видео с себя |

`[СКРИН: HeyGen — экран записи consent-видео с 4-буквенным кодом]`

**Важно про «looks».** У одного аватара может быть много **looks** — разных фото/видео одного и того же человека (другой ракурс, одежда, фон): анфас, профиль, три четверти. Консент нужен **один раз — на самый первый look**. Все следующие looks того же человека добавляются **без повторной верификации** — лимит на аккаунте до 500 looks на аватар.

