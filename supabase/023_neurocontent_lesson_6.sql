## Голос: клонируем и озвучиваем текст

Голос аватара можно взять из встроенной библиотеки HeyGen или клонировать свой — но HeyGen клонирует голос только вместе с видео-аватаром (через тот же консент). Если нужен именно голос отдельно — например, чтобы озвучить текст без аватара в кадре, — быстрее и бесплатнее сделать это через **Minimax**.

**Minimax:** клонирует голос по 10–30 секундам аудио, сохраняя тон, темп и акцент. На бесплатном плане — обычно один клон голоса и небольшой лимит символов текста за раз (перепроверить перед занятием — лимиты у бесплатных голосовых сервисов меняются часто).

### 🛠 Практика 4. Клонируйте голос в Minimax

**🎯 База:** запишите 15–20 секунд своего голоса → клонируйте → озвучьте короткую фразу из сценария (Практика 1).
**🚀 Уровень 2:** тем же клонированным голосом озвучьте текст **на другом языке или с другой интонацией**. Что сохранилось узнаваемым, а что «слетело»?
> 🤔 **Перенос:** какая часть настройки голоса переносится на любой текст, а что нужно перенастраивать под каждую новую задачу?
> 🔓 **Доступ из РБ:** Minimax, по имеющимся данным, доступнее HeyGen из РБ — но перед занятием стоит проверить доступ именно из вашей сети.

## Перевод видео на другие языки

Отдельная функция HeyGen — **Video Translate**: переводит уже готовое видео (не только сделанное в HeyGen) на 175+ языков. Два режима:

| Режим | Что переводится | Липсинк |
|---|---|---|
| **Audio Dubbing** | только звук, голос говорящего сохраняется | Нет |
| **Video Dubbing** | звук + движение губ подстраивается под перевод | Да |

На бесплатном плане на момент написания — до 3 видео/мес, каждое до 3 минут, с автосубтитрами и озвучкой (перепроверить лимиты перед занятием).

`[СКРИН: HeyGen Translate — выбор языка и режима Audio/Video Dubbing]`

