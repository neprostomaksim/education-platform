| **Avatar V** (новая модель) | Всего 15 секунд видео | Тот же консент, что у Digital Twin | Движение и мимика — из видео, а внешность можно взять с **любого** фото |

Ключевое правило: **консент нужен только там, где HeyGen оживляет видео реального человека** (Digital Twin, Avatar V). Photo Avatar его не спрашивает — фото не превращается в «двигающегося» вас, только губы синхронизируются с текстом.

