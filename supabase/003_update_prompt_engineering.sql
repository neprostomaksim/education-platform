## Zero-Shot: без примеров

Вы просто описываете задачу, не показывая примеры:

```
Определи тональность отзыва: "Доставка быстрая, товар качественный, буду заказывать ещё!"
Ответ (одно слово): положительный / отрицательный / нейтральный
```

**Когда работает:**
- Простые, однозначные задачи
- Классификация с очевидными категориями
- Перевод, суммаризация, форматирование

---

## Few-Shot: с примерами

Вы даёте ИИ **2-5 примеров**, чтобы он уловил паттерн:

```
Классифицируй отзывы клиентов по категориям:

"Курьер опоздал на 2 часа" → Логистика
"Товар пришёл с царапиной" → Качество
"Оператор нагрубил по телефону" → Сервис
"Сайт зависает при оплате" → Техника

"Упаковка была мокрой и мятой" → ?
```

**Когда необходимо:**
- Нестандартная классификация
- Специфический формат вывода
- Когда ИИ не угадывает ваши ожидания в Zero-Shot

