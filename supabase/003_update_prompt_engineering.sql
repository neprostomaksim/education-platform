'# Итерация и отладка промптов

Даже эксперты редко получают идеальный результат с первого промпта. Мастерство — в умении итерировать.

---

## Почему первый промпт не работает?

- Вы не учли контекст, который очевиден для вас, но не для ИИ
- Формулировка допускает двойное толкование
- Вы не определили формат или ограничения
- Задача слишком сложная для одного промпта

---

## 5-шаговый цикл итерации

### Шаг 1: Напишите первый промпт
Не тратьте 20 минут на «идеальный» промпт. Начните с чернового варианта.

### Шаг 2: Оцените ответ
Задайте себе вопросы:
- Что в ответе **хорошо**?
- Что **не так**? (слишком длинно? не тот тон? не те данные?)
- Что **отсутствует**?

### Шаг 3: Уточните промпт
Не переписывайте с нуля. Добавьте уточнения:

```
Хорошо, но:
1. Тон слишком формальный — сделай дружелюбнее
2. Добавь конкретные цифры
3. Сократи каждый пункт до 2 предложений
4. Структурируй как таблицу
```

### Шаг 4: Повторите 2-3 раза
Каждая итерация приближает к идеалу.

### Шаг 5: Сохраните финальный промпт
Когда результат устраивает — **сохраните промпт** в личную библиотеку.

---

## Техники отладки

### Когда ИИ отвечает слишком общо:
```
Ответ слишком абстрактный. Перепиши с:
- Конкретными цифрами вместо «значительно»
- Реальными примерами вместо общих фраз
- Пошаговыми действиями вместо общих рекомендаций
```

### Когда ИИ «галлюцинирует»:
```
Не придумывай факты. Если не уверен — напиши «Требует проверки».
Для каждого факта укажи: 🟢 проверено / 🟡 вероятно / 🔴 не уверен.
```

### Когда формат не тот:
```
Перестрой ответ в формате:
| Столбец 1 | Столбец 2 | Столбец 3 |
С нумерацией строк и итоговой строкой.
```

### Когда слишком длинно:
```
Сократи ответ в 3 раза. Оставь только:
- Ключевые выводы
- Конкретные цифры
- Действия (что делать)
Убери: вводные слова, повторы, очевидные вещи.
```

