'# Продвинутые фреймворки промптинга

Фреймворки — это готовые структуры, которые помогают писать промпты быстро и качественно.

---

## 1. RTF — Самый простой

**R**ole → **T**ask → **F**ormat

```
Role: Ты — копирайтер.
Task: Напиши описание товара для карточки на маркетплейсе.
Format: Заголовок (до 80 символов) + 5 буллетов с выгодами + SEO-описание (150 слов).
```

**Когда использовать:** Быстрые, простые задачи.

---

## 2. RISEN — Средний уровень

**R**ole → **I**nstructions → **S**teps → **E**nd goal → **N**arrowing

```
Role: Ты — бизнес-аналитик.
Instructions: Проанализируй конкурентов компании.
Steps:
1. Определи 5 ключевых конкурентов
2. Сравни по цене, качеству, маркетингу
3. Найди незанятые ниши
End goal: Дать рекомендацию по позиционированию.
Narrowing: Только рынок РФ, сегмент B2C, бюджет до 1 млн руб/мес.
```

**Когда использовать:** Задачи средней сложности с чёткими шагами.

---

## 3. COSTAR — Самый мощный

**C**ontext → **O**bjective → **S**tyle → **T**one → **A**udience → **R**esponse

```
Context: Наша компания запускает новый продукт — приложение для медитации. Выход через 2 месяца. Бюджет на маркетинг: $50K.

Objective: Разработать стратегию предзапуска (pre-launch) для набора базы ожидающих пользователей.

Style: Аналитический, data-driven, с конкретными метриками и сроками.

Tone: Профессиональный, но не сухой. Как презентация для совета директоров.

Audience: CMO и CEO стартапа, понимают маркетинг, но хотят конкретики.

Response: Структурированный план с таблицами, таймлайном и бюджетом.
```

**Когда использовать:** Сложные стратегические задачи.

---

## Сравнение фреймворков

| Фреймворк | Сложность | Элементов | Лучше всего для |
|-----------|-----------|-----------|----------------|
| **RTF** | ⭐ Простой | 3 | Быстрые задачи, контент |
| **RISEN** | ⭐⭐ Средний | 5 | Аналитика, пошаговые задачи |
| **COSTAR** | ⭐⭐⭐ Продвинутый | 6 | Стратегия, сложные проекты |

---

## Какой выбрать?

- **Нужен быстрый ответ?** → RTF
- **Есть чёткие шаги?** → RISEN
- **Важен тон и стиль?** → COSTAR
- **Не уверены?** → Начните с RTF, усложняйте при необходимости

