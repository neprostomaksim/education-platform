'# Что такое промпт?

**Промпт** (от англ. *prompt* — подсказка) — это текстовая инструкция, которую вы даёте искусственному интеллекту для получения нужного результата.

> 💡 Качество ответа ИИ на **80% зависит от качества промпта** и лишь на 20% — от выбора модели.

## Почему это важно?

Сравните два запроса:

❌ **Плохой промпт:**
```
Напиши текст
```

✅ **Хороший промпт:**
```
Ты — опытный копирайтер. Напиши продающий текст для лендинга онлайн-школы английского языка. Целевая аудитория — взрослые 25-40 лет. Формат: заголовок + 3 буллета с выгодами. Тон: дружелюбный, без давления.
```

Разница очевидна: второй промпт даёт ИИ **всю необходимую информацию** для качественного ответа.

## Анатомия хорошего промпта

Каждый эффективный промпт содержит 5 элементов:

| Элемент | Описание | Пример |
|---------|----------|--------|
| 🎭 **Роль** | Кем должен быть ИИ | «Ты — опытный маркетолог» |
| 📋 **Контекст** | Фоновая информация | «Компания продаёт онлайн-курсы» |
| 🎯 **Задача** | Что конкретно сделать | «Напиши 5 заголовков для рекламы» |
| 📐 **Формат** | В каком виде выдать | «Нумерованный список с эмоджи» |
| 🚧 **Ограничения** | Чего избегать | «Без клише, максимум 10 слов каждый» |

