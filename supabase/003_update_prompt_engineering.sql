'# Chain-of-Thought (CoT)

**Chain-of-Thought** — техника, при которой мы просим ИИ **рассуждать пошагово**, а не давать готовый ответ сразу.

> 💡 CoT увеличивает точность ответов на **30-50%** в задачах, требующих логики.

---

## Как это работает?

### Без CoT:
```
У Маши 5 яблок. Она отдала Пете половину, потом купила ещё 3.
Сколько у неё яблок?
→ 5 (часто ошибается)
```

### С CoT:
```
У Маши 5 яблок. Она отдала Пете половину, потом купила ещё 3.
Сколько у неё яблок? Рассуждай пошагово.

→ Начальное количество: 5
→ Отдала половину: 5 / 2 = 2.5 → округляем до 2 (отдала 2, осталось 3)
→ Купила ещё 3: 3 + 3 = 6
→ Ответ: 6 яблок ✅
```

---

## Магические фразы

Добавьте одну из этих фраз в конец промпта:

```
Давай подумаем шаг за шагом.
```

```
Рассуждай последовательно, показывая каждый шаг.
```

```
Прежде чем дать ответ, разложи задачу на этапы.
```

---

## Когда использовать CoT?

| Тип задачи | Без CoT | С CoT |
|-----------|---------|-------|
| Математика | ⚠️ Часто ошибки | ✅ Точный расчёт |
| Логические задачи | ⚠️ Угадывает | ✅ Обоснованный ответ |
