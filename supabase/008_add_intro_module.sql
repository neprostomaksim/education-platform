## Почему ИИ врет (галлюцинации)

Иногда нейросеть уверенно выдумывает факты, законы или несуществующие книги. В профессиональной среде это называют **галлюцинациями**.

Почему это происходит? Если ИИ не знает точного ответа (например, нет данных в его обучающей выборке), его алгоритм все равно обязан продолжить текст. Он выбирает слова, которые грамматически идеально подходят друг к другу, но фактически не соответствуют реальности. Нейросеть не испытывает стыда, потому что не знает разницы между правдой и вымыслом — она лишь строит красивую словесную цепь.

Как с этим бороться:
1. Ограничивайте «фантазию» ИИ (просите писать только на основе предоставленного текста).
2. Требуйте указывать ссылки на источники.
3. Всегда перепроверяйте критически важные факты.

