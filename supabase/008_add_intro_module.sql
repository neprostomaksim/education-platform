## Мультиагентная система

Решение — создать команду из нескольких специализированных агентов, каждый из которых делает только свою часть работы. Такую связку называют **мультиагентной системой**.

Любая мультиагентная система — это цепочка промптов, которые работают последовательно или параллельно.

## Два способа связать агентов

### 1. Ручной способ (Копипаст)
Вы сами создаете отдельных агентов в интерфейсе нейросети (например, в разных вкладках чата) и переносите результаты между ними:
1. Вы даете задачу **Агенту-Маркетологу**. Он выдает вам структуру и тезисы.
2. Вы копируете этот текст и отправляете его **Агенту-Копирайтеру**. Он пишет статью.
3. Вы берете готовую статью и передаете её **Агенту-Редактору**. Он находит ошибки и правит стиль.

Это самый простой способ начать. Он дает вам полный контроль над каждым этапом работы.

### 2. Автоматический способ
Агенты связаны программно. Вы даете задачу на старте, а нейросети общаются между собой сами без вашего участия. Агент-Маркетолог автоматически шлет ТЗ Копирайтеру, тот передает текст Редактору, а вам на экран выводится уже готовый, отшлифованный результат.

## Что важно запомнить:
Для сложных задач создают команду из нескольких ИИ-агентов. Вы можете передавать работу между ними вручную (копируя текст из чата в чат) или настроить автоматическую цепочку, где агенты работают самостоятельно.
