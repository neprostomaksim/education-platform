## ⚠️ Важно: ИИ может выдумывать (галлюцинации)

Главное, что нужно принять с самого начала: **ИИ иногда выдумывает.** Он может выдать несуществующие факты, цифры, номера, пункты нормативных актов или ссылки — и подать это **уверенно, как будто так и есть**. Это называют «галлюцинациями». ИИ не врёт со зла — он достраивает правдоподобный текст, даже когда чего-то не знает.

Что это значит на практике для руководителя:

- **Любую важную информацию от ИИ проверяем** — суммы, даты, номера, цитаты НПА, выводы. Особенно то, что уйдёт клиенту или в отчёт.
- **ИИ — это черновик, а не источник истины.** Он ускоряет работу, но ответственность и проверка — на человеке.
- Риск галлюцинаций **снижает правило в запросе**: *«если данных нет — напиши "не указано", не выдумывай»*. Мы будем добавлять его во всех агентах курса.
- Для работы с документами есть отдельный инструмент, который отвечает **только по вашим файлам** и почти не выдумывает, — **NotebookLM** (Модуль 3).

> 🔒 Вывод: ИИ отлично экономит время на черновиках, но там, где цена ошибки высока, — перепроверяйте. Это не недостаток инструмента, а правило работы с ним.

## Почему ответы ИИ такие разные (аналогия с сотрудником)

Один и тот же вопрос можно задать плохо и хорошо — и ответ будет как небо и земля. Разница не в «настроении» ИИ, а в том, **как составлен запрос**.

Представьте, что вы поручаете задачу сотруднику. Чтобы он сделал хорошо, ему нужно:

- **кто он в этой задаче** — его роль (бухгалтер посмотрит иначе, чем юрист);
- **что именно сделать** — понятная задача;
- **что нужно знать** — входные данные и правила;
- **как оформить результат** — вид, объём, при желании пример.

Скажешь сотруднику только «сделай что-нибудь по договору» — получишь непонятно что. Дашь роль, задачу, данные и формат — получишь то, что нужно. **С ИИ ровно так же.** Из этих четырёх вещей и складывается формула запроса.

## Формула промпта: Р · З · К · Ф

| Буква | Блок | Что писать |
|---|---|---|
| **Р** | Роль | Кто ИИ в задаче: «Ты — ассистент руководителя аудиторской фирмы…» |
| **З** | Задача | Что сделать — одним ясным действием |
| **К** | Контекст | Входные данные и правила: «существенное — дословно», «нет данных — пиши "не указано"» |
| **Ф** | Формат | В каком виде ответ: таблица, три блока, письмо |

Это каркас на каждый день. Дальше разберём каждый элемент подробно — с примерами и нюансами.

## Разбираем по элементам

### 1. 🎭 Роль — кто ты?

Роль задаёт тон, глубину и терминологию.

- ❌ *«Расскажи про договоры»*
- ✅ *«Ты — ассистент руководителя аудиторской фирмы, ничего не оцениваешь — только структурируешь то, что написано»*

Один вопрос «от лица» разных ролей даёт разные ответы. Хотите объёмную картину — попросите ответить последовательно от лица налогового специалиста, методолога и руководителя группы.

### 2. 📋 Задача — что сделать?

Конкретный глагол и **одна** главная задача.

- ❌ *«Что-нибудь по этому документу»*
- ✅ *«Составь краткую выжимку документа с ключевыми условиями и рисками»*

**Правило одной задачи:** нужно и проанализировать, и написать письмо, и составить план — это три отдельных запроса (или цепочка шагов, см. ниже).

### 3. 🔍 Контекст — что важно знать?

Дайте данные и правила игры. **Приём «конверт»** — оборачивайте данные в теги, чтобы ИИ отличал их от инструкций:

```
<документ>
… обезличенный текст …
</документ>
```

## Форматы вывода — выбираем осознанно

Если не задать формат, ИИ выдаёт «стену текста», из которой суть придётся вытаскивать самому. Задавайте формат явно:

| Формат | Когда просить |
|---|---|
| **Таблица** | сравнения, разборы, планы, реестры |
| **Нумерованный список** | шаги, рекомендации, повестка |
| **Три блока** (суть · риски · действия) | управленческая выжимка для решения |
| **Готовое письмо** | переписка с клиентом/партнёром |
| **Структура слайдов** | подготовка к совещанию/совету |
| **Строка через табуляцию** | данные для вставки в Excel |
| **Чек-лист** | контроль, приёмка, «ничего не забыть» |

Если нужен конкретный вид ответа — **покажите пример прямо в запросе**: «оформи вот так: …» и приведите образец строки или абзаца.

## Ограничения — рамки, которые дают качество

Ограничения превращают «поток сознания» в точный ответ. Добавляйте 2–4 к каждой важной задаче:

| Тип | Пример |
|---|---|
| **Объём** | «не более 200 слов», «3–5 пунктов» |
| **Аудитория** | «для совета директоров, без технических деталей» |
| **Тон** | «деловой, без канцелярита и воды» |
| **Исключения** | «без общих фраз "повысить эффективность"», «не предлагай снижать цены» |
| **Антигаллюцинации** | «нет данных — пиши "требует уточнения", не выдумывай цифры и нормы» |

## Пошаговая постановка — разбивай сложное на цепочку

Если свалить в один промпт «проанализируй, напиши стратегию, сделай письмо и посчитай бюджет» — качество каждого куска будет низким. ИИ, как и человек, лучше работает последовательно. Разбивайте большую задачу на 3–5 запросов **в одном чате** (модель помнит контекст):

```
Шаг 1: «Разбери договор — дай ключевые условия и риски»
Шаг 2: «Из рисков выбери 3 самых важных для клиента»
Шаг 3: «Оформи эти 3 риска в письмо клиенту по деловому стилю»
```

Каждый следующий шаг опирается на предыдущий. Так вы контролируете результат на каждом этапе, а не получаете «всё сразу и мимо».

## Плохой промпт → хороший промпт

**❌ Плохо:** `Разбери документ` → рыхлый пересказ, часть выдумана.

**✅ Хорошо:**
```
Роль: ты — ассистент руководителя, готовишь выжимку документов.
Правила: существенное — дословно; нет данных — «не указано», не додумывай.
Задача: сделай структурную выжимку документа ниже.
Формат (без вступлений): 1) о чём документ; 2) ключевые условия;
3) риски и на что обратить внимание.
Ограничение: не более 1 страницы, деловой тон.
<документ> … </документ>
```

## Итерируйте — первый ответ редко лучший

Работайте с ИИ как со стажёром — давайте обратную связь. Три рабочие фразы:
1. *«Сделай конкретнее пункт X — добавь цифры/ссылку»*
2. *«Перепиши короче, для совещания»*
3. *«Оставь только 3 главных пункта и обоснуй, почему они»*

## Контекст и память чата

Важно понимать, как ИИ «помнит» разговор:

- **Внутри одного чата** ИИ помнит, что вы писали выше, — поэтому можно уточнять и дорабатывать ответ, не повторяя всё заново.
- **Несколько задач в одном чате — можно, если они об одном контексте** (например, разобрали договор, а потом попросили письмо по нему — ИИ уже «в теме»). Если задача новая, но связана — просто **подгрузите новый контекст** в тот же чат.
- А вот **совсем не связанную** задачу лучше начинать в **новом чате** — иначе ИИ будет тянуть лишний контекст и путаться.
- **Память не бесконечна:** в очень длинном чате ранние детали начинают теряться, и ответы «плывут». Если ИИ путается — начните новый чат.
- Если задача **повторяется** (каждую неделю пишете однотипные письма, разбираете договоры) — не переписывайте инструкцию каждый раз. Для таких задач заводят **агента (Gem-бота)** — это следующий урок.

