## Три опоры контроля

1. **Правила** — что можно и нельзя (ИИ-политика ниже).
2. **Инструменты** — единые обезличивание и агенты, чтобы не было «кто как умеет».
3. **Проверка** — выборочный контроль, что уходит в облако и что попадает в отчёт.

## ИИ-политика фирмы (шаблон)

**Можно:**
- обезличенные данные в бесплатные ИИ-сервисы;
- черновики (письма, выжимки, разбор договоров, замечания) — с обязательной проверкой человеком;
- методологию фирмы (памятки, нормы, справочники) — как файлы-знания Gem.

**Нельзя:**
- данные клиента (наименование, УНП, счета, ФИО, суммы) в облако без обезличивания;
- принимать вывод ИИ как итоговое суждение (существенность, вывод о нарушении);
- цитировать НПА со слов ИИ — только дословно из акта, актуальность из правовой базы;
- грузить данные клиента в файлы-знания Gem (на бесплатном тарифе могут уйти в обучение моделей).

**Всегда:**
- обезличивание перед облаком, карта замен — только локально;
- человек проверяет и подписывает;
- у команды — единые инструменты и один ответственный за ИИ.

## Чек-лист внедрения и безопасности (уносите с собой)

- ☐ Назначен ответственный за ИИ в фирме.
- ☐ Определены 1–2 задачи для пилота.
- ☐ У всех участников: VPN, аккаунт, доступ к Gemini/NotebookLM.
- ☐ Единый макрос/инструкция обезличивания у команды.
- ☐ Библиотека общих Gem (суммаризатор, письмо, разбор договора и т.д.).
- ☐ Записана ИИ-политика (можно/нельзя/всегда) и доведена до команды.
- ☐ Правило: данные клиента — только обезличенные; карта замен локально.
- ☐ Правило: ИИ — черновик, человек — проверка и суждение.
- ☐ НПА — дословно из акта, не со слов ИИ.
- ☐ Выборочная проверка: что уходит в облако и что идёт в отчёт.

## Как проверять команду (без микроменеджмента)

- **Точечно, по результату:** сверять итоговые документы, а не следить за каждым запросом.
- **По рискам:** особое внимание — местам, где ИИ мог «дорисовать» (цитаты НПА, суммы, существенность).
- **Через обучение:** разбирать типовые ошибки (пересказ нормы вместо цитаты, потеря карты замен) на общих встречах.

