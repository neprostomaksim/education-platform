## Формула промпта: Р · З · К · Ф

Хороший запрос к ИИ — это не «магическое слово», а четыре понятных блока:

| Буква | Блок | Что писать |
|---|---|---|
| **Р** | Роль | Кто ИИ в этой задаче: «Ты — ассистент аудитора…» |
| **З** | Задача | Что конкретно сделать — одним ясным действием |
| **К** | Контекст | Входные данные, ограничения, правила: «существенное — дословно», «нет данных — пиши "не указано"» |
| **Ф** | Формат | В каком виде ответ: таблица, строка через табуляцию, три блока |

Это короткая рабочая версия на каждый день. Ниже — тот же навык подробно, с примерами «плохо → хорошо» и двумя усилителями для важных задач.

## Как говорить с ИИ: разбираем по элементам

Промпт — это ваше техзадание для ИИ. Размытое ТЗ = бесполезный ответ; точное ТЗ = результат уровня дорогого консультанта. Разница между «ИИ ерунду выдаёт» и «ИИ выдал готовое решение» почти всегда в качестве промпта, а не в модели. Разберём каждый элемент.

### 1. 🎭 Роль — кто ты?

Роль задаёт тон, глубину и терминологию ответа.

- ❌ *«Расскажи про кредитные договоры»*
- ✅ *«Ты — ассистент аудитора с опытом проверки кредитных договоров. Точность важнее полноты, ничего не оцениваешь — только переносишь то, что написано»*

Один и тот же вопрос «от лица» разных ролей даёт разные ответы. Хотите оценить операцию со всех сторон — попросите ИИ ответить последовательно от лица налогового специалиста, методолога и руководителя группы.

### 2. 📋 Задача — что сделать?

Конкретный глагол и одна главная задача.

- ❌ *«Что-нибудь по этому договору»*
- ✅ *«Извлеки условия кредитного договора строго по 16 полям в таблицу рабочего документа»*

**Правило одной задачи:** один промпт — одно действие. Нужно и извлечь условия, и написать замечание, и составить письмо — это три отдельных промпта (или три шага внутри диалога).

### 3. 🔍 Контекст — что важно знать?

Дайте входные данные и правила игры. Чем больше релевантного контекста — тем точнее ответ.

- ❌ *«Разбери договор»*
- ✅ правила прямо в промпте: *«существенные условия приводи дословно, после значения ставь пункт-источник [п.__], нет условия — пиши "не указано", не додумывай»*

**Приём «конверт»:** оборачивайте данные в теги, чтобы ИИ отличал ваши данные от инструкций:

```
<текст_договора>
… обезличенный текст договора с токенами КОМПАНИЯ_001 …
</текст_договора>
```

### 4. ⚠️ Формат и ограничения — как должен выглядеть ответ и чего НЕ делать

Формат превращает «поток сознания» в структуру. Ограничения задают границы.

- *«Формат: таблица "Поле → Значение [пункт]", затем строка через табуляцию для вставки в Excel»*
- *«Без вступлений и заключений — только результат»*
- *«Если данных нет — напиши "не указано", НЕ придумывай числа и нормы»* (антигаллюцинации)
- *«Язык деловой, по нашим "Нормам языка": числа 18 359,75, даты без "г.", кавычки-ёлочки»*

### 5. 📝 Образцы — покажи пример

Один хороший пример стоит десяти абзацев объяснений. Покажите, как должна выглядеть строка результата:

```
Формат строки — вот так:
Кредитный договор	КД-2024/117	15.03.2024	БАНК_001	…
```

## Плохой промпт → хороший промпт

Разница на одном примере.

**❌ Плохо:**
```
Сделай таблицу по договору
```
Ответ: рыхлый пересказ, часть полей выдумана, часть пропущена. Бесполезно.

**✅ Хорошо (все элементы):**
```
Роль: ты — ассистент аудитора, извлекаешь условия кредитных договоров.
Ничего не оцениваешь, только переносишь то, что написано.

Контекст и правила:
- существенные условия (ставка, суммы, сроки, пени, ковенанты) — дословно;
- после значения ставь пункт-источник [п.__];
- нет условия в тексте — пиши «не указано в договоре», не додумывай;
- противоречие или разные места — «проверить вручную» + оба места.

Задача: извлеки условия строго по 16 полям в таблицу рабочего документа.

Формат (без вступлений):
А) таблица «Поле → Значение [пункт]»;
Б) строка через табуляцию для вставки в Excel;
В) блок «⚠️ Требует проверки аудитором».

<текст_договора>
… обезличенный текст …
</текст_договора>
```
Ответ: собранная таблица, где пустые поля честно помечены «не указано», а противоречие по ставке — «проверить вручную». Это и есть готовый рабочий черновик.

## Итерируйте — первый ответ редко лучший

Самая частая ошибка: получить первый ответ, решить «ИИ не работает» и закрыть чат. Работайте с ИИ как со стажёром — давайте обратную связь и просите доработать. Три рабочие фразы:

1. *«Сделай конкретнее пункт X — добавь ссылку на пункт договора»*
2. *«Перепиши это в формате строки через табуляцию для Excel»*
3. *«Оставь в блоке проверки только поля "не указано" и "проверить вручную"»*

## Чек-лист «признаки рабочего промпта»

Перед отправкой пробегитесь по списку:

- ✅ задана **роль**;
- ✅ одна **чёткая задача** конкретным глаголом;
- ✅ есть **контекст и правила** (в т.ч. «нет данных → не указано»);
- ✅ задан **формат вывода**;
- ✅ по возможности — **образец** желаемого результата.

> **Правило «достаточно базы»:** для простых задач хватает задачи + контекста + формата. Для важных — добавляйте роль и образец.

