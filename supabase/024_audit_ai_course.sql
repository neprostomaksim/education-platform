## ИИ-политика фирмы (шаблон)

**Можно:**
- обезличенные данные в бесплатные ИИ-сервисы;
- черновики (письма, выжимки, разбор договоров, замечания) — с обязательной проверкой человеком;
- методологию фирмы (памятки, нормы, справочники) — как файлы-знания Gem.

**Нельзя:**
- данные клиента (наименование, УНП, счета, ФИО, суммы) в облако без обезличивания;
- принимать вывод ИИ как итоговое суждение (существенность, вывод о нарушении);
- цитировать НПА со слов ИИ — только дословно из акта, актуальность из правовой базы;
- грузить данные клиента в файлы-знания Gem (на бесплатном тарифе могут уйти в обучение моделей).

**Всегда:**
- обезличивание перед облаком, карта замен — только локально;
- человек проверяет и подписывает;
- у команды — единые инструменты и один ответственный за ИИ.

