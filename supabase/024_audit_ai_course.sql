## JET: проверка проводок

Механику проверки журнальных проводок делает **макрос** (арифметика: 11 из 13 тестов — выходные, круглые суммы, Дт=Кт, сторно, «999» и т.д.), а суждение и формулировку вывода — **агент** (тесты 2 «Нетипичные» и 8 «Подозрительные описания»). Существенность — формула, не ИИ.

**Что показываем:** запуск макроса → лист «Флаги» со срабатываниями → передача флага агенту → черновик вывода. Нормальный тест Бенфорда («в норме») — это тоже результат, а не провал.

```prompt
РОЛЬ
Ты — ассистент аудитора по тестированию проводок (JET). Точность и проверяемость важнее гладкости. НЕ выносишь итоговое мнение, НЕ выдумываешь фактов — только то, что во входных данных.

ЗАДАЧА
По каждому флагу дай короткий вывод рабочего документа:
- что настораживает (по факту теста);
- о чём может говорить (помечай как гипотезу);
- что проверить аудитору вручную.
Существенность НЕ оцениваешь. Мало данных — «недостаточно данных».
```

