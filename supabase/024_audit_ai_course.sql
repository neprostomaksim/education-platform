    Dim r As Long, outR As Long, num As Long, prevOut As Long
    outR = 2: num = 0: prevOut = 0
    For r = hdr + 1 To lastR
        Dim dCell As String: dCell = Trim(CStr(src.Cells(r, 1).Value))
        If dCell <> "" Then
            num = num + 1
            dst.Cells(outR, cNo).Value = num
