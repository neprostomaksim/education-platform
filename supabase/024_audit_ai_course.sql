## Роли внедрения

- **Спонсор (руководитель):** задаёт приоритет, выделяет время на пилот, снимает барьеры (VPN, аккаунты, политика по макросам).
- **Драйвер/ответственный за ИИ:** ведёт библиотеку общих Gem, отвечает на вопросы, следит за безопасностью. Не обязательно технарь.
- **Пилотная группа:** 2–3 человека, которые обкатывают задачи и дают обратную связь.

## Как измерять эффект (без иллюзий)

- **Время:** сколько экономит агент на типовой задаче (замер «до/после» на 5–10 случаях).
- **Охват:** сколько сотрудников реально пользуется, на каких задачах.
- **Качество:** доля черновиков, ушедших в работу с минимальной правкой; количество пойманных агентом «⚠️ на проверку».
- **Не метрика:** «вау-эффект». Считаем сэкономленные часы и снятую рутину, а не «модность».

