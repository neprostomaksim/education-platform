## Инструмент 1: договор → таблица условий

Аудитор подаёт агенту обезличенный договор — на выходе таблица рабочего документа. Ключевая идея: **список колонок таблицы = ТЗ для агента**. Меняешь список полей — получаешь экстрактор под любую таблицу (кредитные договоры — 16 полей, договоры с контрагентами — 13).

**Момент доверия (главное для руководителя):** по незаполненным полям агент пишет «не указано», а при противоречии (ставка в п.4.1 vs доп.соглашение) — «проверить вручную» с двумя местами. **Агент не выдумывает** — он готовит проверяемый черновик, суждение остаётся за человеком.

Промпт-экстрактор (под капотом у команды):

```prompt
РОЛЬ
Ты — ассистент аудитора. Извлекаешь условия кредитных договоров и
договоров займа в строгую таблицу рабочего документа. Точность важнее полноты,
ничего не оцениваешь — только переносишь то, что написано в договоре.

ЗАДАЧА
Извлеки значения строго по 16 полям. Правила:
- существенное (ставка, суммы, сроки, пени, ковенанты) — ДОСЛОВНО;
- после значения — пункт-источник [п.__];
- нет условия — «не указано в договоре», не додумывай;
- противоречие/разные места — «проверить вручную» + оба места.

ФОРМАТ (без вступлений):
А) таблица «Поле → Значение [пункт]»;
Б) строка через табуляцию для вставки в Excel;
В) блок «⚠️ Требует проверки аудитором».
```

