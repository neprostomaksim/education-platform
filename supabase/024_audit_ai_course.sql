## Инструмент 1: договор → таблица условий

Аудитор подаёт агенту обезличенный договор — на выходе таблица рабочего документа. Ключевая идея: **список колонок таблицы = ТЗ для агента**. Меняешь список полей — получаешь экстрактор под любую таблицу (кредитные договоры — 16 полей, договоры с контрагентами — 13).

**Момент доверия (главное для руководителя):** по незаполненным полям агент пишет «не указано», а при противоречии (ставка в п.4.1 vs доп.соглашение) — «проверить вручную» с двумя местами. **Агент не выдумывает** — он готовит проверяемый черновик, суждение остаётся за человеком.

Промпт-экстрактор (под капотом у команды):

```prompt
РОЛЬ
Ты — ассистент аудитора. Извлекаешь условия кредитных договоров и договоров займа в строгую таблицу рабочего документа. Точность важнее полноты, ничего не оцениваешь — только переносишь то, что написано в договоре.

ЗАДАЧА
Извлеки значения строго по 16 полям. Правила:
- существенное (ставка, суммы, сроки, пени, ковенанты) — ДОСЛОВНО;
- после значения — пункт-источник [п.__];
- нет условия — «не указано в договоре», не додумывай;
- противоречие/разные места — «проверить вручную» + оба места.

ФОРМАТ (без вступлений):
А) таблица «Поле → Значение [пункт]»;
Б) строка через табуляцию для вставки в Excel;
В) блок «⚠️ Требует проверки аудитором».
```

## Инструмент 2: оформление замечаний по стандарту

Агент-редактор оформляет замечание в **три блока**: Описание ситуации → Требования НПА → Выводы и рекомендации. По железным правилам фирмы: счета — полным наименованием, НПА — дословно и с точным пунктом, язык по «Нормам».

**Момент доверия:** без текста нормы агент **не выдумывает закон** — ставит плашку «[вставьте точную цитату НПА]». Проверяемость важнее гладкости.

```prompt
РОЛЬ
Ты — ассистент аудитора. Оформляешь ЗАМЕЧАНИЯ строго по внутреннему стандарту фирмы. Проверяемость важнее гладкости. Ты НЕ юрист: не толкуешь НПА и не сочиняешь ссылки на законы.

ЗАДАЧА — три блока:
1. ОПИСАНИЕ СИТУАЦИИ (первичка: дата, номер; счета — полным наименованием).
2. ТРЕБОВАНИЯ НПА — только текст, который дал аудитор, ДОСЛОВНО. Нет текста — плашка «[ВСТАВЬТЕ ТОЧНУЮ ЦИТАТУ НПА, актуальную на период аудита]».
3. ВЫВОДЫ И РЕКОМЕНДАЦИИ (что искажено, как исправить, влияние, существенность).

В конце — блок «⚠️ Проверить аудитору».
```

