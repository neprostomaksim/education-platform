## Разбираем по элементам

### 1. 🎭 Роль — кто ты?

Роль задаёт тон, глубину и терминологию.

- ❌ *«Расскажи про договоры»*
- ✅ *«Ты — ассистент руководителя аудиторской фирмы, ничего не оцениваешь — только структурируешь то, что написано»*

Один вопрос «от лица» разных ролей даёт разные ответы. Хотите объёмную картину — попросите ответить последовательно от лица налогового специалиста, методолога и руководителя группы.

### 2. 📋 Задача — что сделать?

Конкретный глагол и **одна** главная задача.

- ❌ *«Что-нибудь по этому документу»*
- ✅ *«Составь краткую выжимку документа с ключевыми условиями и рисками»*

**Правило одной задачи:** нужно и проанализировать, и написать письмо, и составить план — это три отдельных запроса (или цепочка шагов, см. ниже).

### 3. 🔍 Контекст — что важно знать?

Дайте данные и правила игры. **Приём «конверт»** — оборачивайте данные в теги, чтобы ИИ отличал их от инструкций:

```
<документ>
… обезличенный текст …
</документ>
```

## Форматы вывода — выбираем осознанно

Если не задать формат, ИИ выдаёт «стену текста», из которой суть придётся вытаскивать самому. Задавайте формат явно:

| Формат | Когда просить |
|---|---|
| **Таблица** | сравнения, разборы, планы, реестры |
| **Нумерованный список** | шаги, рекомендации, повестка |
| **Три блока** (суть · риски · действия) | управленческая выжимка для решения |
| **Готовое письмо** | переписка с клиентом/партнёром |
| **Структура слайдов** | подготовка к совещанию/совету |
| **Строка через табуляцию** | данные для вставки в Excel |
| **Чек-лист** | контроль, приёмка, «ничего не забыть» |

> 💡 Один хороший **образец** желаемого вида в промпте стоит десяти абзацев объяснений — покажите ИИ пример строки/абзаца, как надо.

## Ограничения — рамки, которые дают качество

Ограничения превращают «поток сознания» в точный ответ. Добавляйте 2–4 к каждой важной задаче:

| Тип | Пример |
|---|---|
| **Объём** | «не более 200 слов», «3–5 пунктов» |
| **Аудитория** | «для совета директоров, без технических деталей» |
| **Тон** | «деловой, без канцелярита и воды» |
| **Исключения** | «без общих фраз "повысить эффективность"», «не предлагай снижать цены» |
| **Антигаллюцинации** | «нет данных — пиши "требует уточнения", не выдумывай цифры и нормы» |

## Пошаговая постановка — разбивай сложное на цепочку

Если свалить в один промпт «проанализируй, напиши стратегию, сделай письмо и посчитай бюджет» — качество каждого куска будет низким. ИИ, как и человек, лучше работает последовательно. Разбивайте большую задачу на 3–5 запросов **в одном чате** (модель помнит контекст):

```
Шаг 1: «Разбери договор — дай ключевые условия и риски»
Шаг 2: «Из рисков выбери 3 самых важных для клиента»
Шаг 3: «Оформи эти 3 риска в письмо клиенту по деловому стилю»
```

Каждый следующий шаг опирается на предыдущий. Так вы контролируете результат на каждом этапе, а не получаете «всё сразу и мимо».

## Плохой промпт → хороший промпт

**❌ Плохо:** `Разбери документ` → рыхлый пересказ, часть выдумана.

**✅ Хорошо:**
```
Роль: ты — ассистент руководителя, готовишь выжимку документов.
Правила: существенное — дословно; нет данных — «не указано», не додумывай.
Задача: сделай структурную выжимку документа ниже.
Формат (без вступлений): 1) о чём документ; 2) ключевые условия;
3) риски и на что обратить внимание.
Ограничение: не более 1 страницы, деловой тон.
<документ> … </документ>
```

