### Инструкция: как завести любой наш макрос

1. Открыть нужный файл → `Alt+F11` (редактор VBA).
2. File → Import File → выбрать `.bas` (или Insert → Module и вставить код).
3. Закрыть редактор → `Alt+F8` → выбрать процедуру (`Обезличить` / `ВернутьДанные`) → Run.
4. Если Excel ругается на макросы — включить их для файла (Разработчик → Безопасность макросов) или идти запасным ручным путём (ниже).

