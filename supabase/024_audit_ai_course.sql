Ручной **«Найти и заменить»** по той же логике: составить список реальных значений → заменить на токены → вести карту замен вручную в отдельном файле. Медленнее, но безопасность та же.

## Честный нюанс (обязательно)

Макрос маскирует **значение ячейки целиком**. Если контрагент или УНП сидят **внутри длинной строки** (содержание проводки, субконто) — вкрапление он не поймает. **Свободный текст проверяем глазами.**

## Агент «Генератор макросов»

Макрос анонимизации не с неба упал — такой макрос можно получить у агента. Аудитор описывает задачу словами → агент выдаёт готовый VBA-код + инструкцию вставки. Это переносимый навык: каждый уносит агента на полку и генерит своё уже в спокойном темпе.

> Формат в зале — **демо-ведомый**, не массовая генерация у всех сразу. Для уровня «что-то трогали» массовая генерация макросов = хаос из ошибок.

### Формула входа (главное — этому и учим)

Хороший запрос агенту = 4 вещи: **(1)** что за таблица (лист, колонки) · **(2)** что сделать · **(3)** что на выходе (новый лист / покраска / колонка) · **(4)** особые правила.

- ❌ Плохо: «сделай макрос для проводок».
- ✅ Хорошо: «на листе Данные, колонка D — суммы; выдели жёлтым строки, где сумма выше 100000; исходные данные не трогай».

