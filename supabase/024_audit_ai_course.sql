## Как говорить с ИИ: разбираем по элементам

Промпт — это техзадание для ИИ. Размытое ТЗ = бесполезный ответ; точное = результат уровня дорогого консультанта.

### 1. 🎭 Роль — кто ты?

- ❌ *«Расскажи про договоры»*
- ✅ *«Ты — ассистент руководителя аудиторской фирмы, ничего не оцениваешь — только структурируешь то, что написано»*

Один вопрос «от лица» разных ролей даёт разные ответы. Хотите объёмную картину — попросите ответить последовательно от лица налогового специалиста, методолога и руководителя группы.

### 2. 📋 Задача — что сделать?

Конкретный глагол и **одна** главная задача.

- ❌ *«Что-нибудь по этому документу»*
- ✅ *«Составь краткую выжимку документа с ключевыми условиями и рисками»*

**Правило одной задачи:** нужно и проанализировать, и написать письмо, и составить план — это три отдельных запроса.

### 3. 🔍 Контекст — что важно знать?

Дайте данные и правила игры.

- ✅ *«существенное приводи дословно, нет данных — пиши "не указано", не додумывай»*

**Приём «конверт»:** оборачивайте данные в теги, чтобы ИИ отличал их от инструкций:

```
<документ>
… обезличенный текст …
</документ>
```

### 4. ⚠️ Формат и ограничения — как выглядит ответ и чего НЕ делать

- *«Формат: три блока — суть, риски, что проверить»*
- *«Без вступлений — только результат»*
- *«Если данных нет — "не указано", НЕ придумывай числа и нормы»* (антигаллюцинации)

