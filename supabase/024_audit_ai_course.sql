    Dim d As Object: Set d = CreateObject("Scripting.Dictionary")
    Dim last As Long, r As Long
    last = ws.Cells(ws.Rows.Count, 2).End(xlUp).Row
    For r = 2 To last
        Dim key As String: key = Trim(CStr(ws.Cells(r, 2).Value))
