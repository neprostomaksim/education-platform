## NotebookLM: спроси у своих документов

Бесплатный инструмент, отвечает **только по загруженным документам** и даёт ссылку на фрагмент. Нет ответа в источнике — говорит «не найдено», а не выдумывает. Для НПА, учётной политики, договоров — то, что нужно.

**Настройка:** New Notebook → Add source → «Настройка чата → Свой вариант» → вставить инструкцию агента → задавать вопросы.

> 📄 **Что можно загрузить как источник:** PDF, Google Docs, текстовые файлы (.txt), ссылки на страницы и просто **вставленный текст** (Add source → Copied text). ❌ **Excel (.xlsx) NotebookLM не принимает** — это инструмент для документов, а не для таблиц. Для практики скачайте внизу **учебный документ (.txt)** или вставьте его текстом.

**Провокация на честность (главный аргумент доверия):** спросить то, чего в документе нет → агент отвечает «в источниках не найдено». Это доказательство, что инструмент не галлюцинирует.

```prompt
РОЛЬ
Ты — ассистент аудитора. Отвечаешь СТРОГО по загруженным документам (нормативные акты, методички). Ничего не додумываешь.

ПРАВИЛА
- Каждый ответ — со ссылкой на конкретный фрагмент источника.
- Существенные формулировки — ДОСЛОВНО.
- Нет ответа в источниках — так и напиши: не найдено. Не выдумывай.
- Актуальность акта на сегодня не оцениваешь — это аудитор проверяет в правовой базе.
```

> ⚖️ **Честная граница:** NotebookLM — не живая правовая база. Актуальную редакцию берут в Эталоне, а NotebookLM помогает быстро в ней разобраться.

## JET: проверка проводок

Механику проверки журнальных проводок делает **макрос** (арифметика: 11 из 13 тестов — выходные, круглые суммы, Дт=Кт, сторно, «999» и т.д.), а суждение и формулировку вывода — **агент** (тесты 2 «Нетипичные» и 8 «Подозрительные описания»). Существенность — формула, не ИИ.

**Что показываем:** запуск макроса → лист «Flags» со срабатываниями → передача флага агенту → черновик вывода. Нормальный тест Бенфорда («в норме») — это тоже результат, а не провал.

```prompt
РОЛЬ
Ты — ассистент аудитора по тестированию проводок (JET). Точность и проверяемость важнее гладкости. НЕ выносишь итоговое мнение, НЕ выдумываешь фактов — только то, что во входных данных.

ЗАДАЧА
По каждому флагу дай короткий вывод рабочего документа:
- что настораживает (по факту теста);
- о чём может говорить (помечай как гипотезу);
- что проверить аудитору вручную.
Существенность НЕ оцениваешь. Мало данных — «недостаточно данных».
```

## Что это даёт команде (управленческий вывод)

- **NotebookLM** снимает риск «ИИ придумал норму»: ответ всегда со ссылкой на источник.
- **JET** переносит рутинную арифметику на макрос, а человека оставляет на суждении.
- **Общая логика проверок:** арифметику — машине, суждение — человеку, итог — аудитору.

