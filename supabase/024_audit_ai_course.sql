- ☐ Единый макрос/инструкция обезличивания у команды.
- ☐ Библиотека общих Gem (суммаризатор, письмо, разбор договора и т.д.).
- ☐ Записана ИИ-политика (можно/нельзя/всегда) и доведена до команды.
- ☐ Правило: данные клиента — только обезличенные; карта замен локально.
- ☐ Правило: ИИ — черновик, человек — проверка и суждение.
- ☐ НПА — дословно из акта, не со слов ИИ.
- ☐ Выборочная проверка: что уходит в облако и что идёт в отчёт.

