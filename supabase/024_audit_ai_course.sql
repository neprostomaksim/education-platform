Эти макросы — рутина исполнителя; руководителю важно знать, что они есть и что делают. Запуск одинаков: открыть учебный файл → `Alt+F11` → Import File → выбрать `.bas` → `Alt+F8` → выбрать процедуру → Run.

| Макрос | Что делает | Запуск |
|---|---|---|
| Обезличивание | маскирует данные токенами | `Обезличить` / `ВернутьДанные` |
| JET | 13 механических тестов проводок | `ПрогнатьВсеТесты` |
| Сверка ОСВ | арифметическая сверка оборотки | `ОСВ_Проверка` |
| Сверка списков | учёт ↔ акт контрагента | `Сверка_Списков` |
| Выборка | аудиторская выборка (3 метода) | `Выборка` |

У учебных файлов внутри есть листы **«Ключ_аномалий» / «Ключ_расхождений»** для самопроверки.

## Как завести Gem

1. Десктоп → gemini.google.com → **Gems Manager → New Gem**.
2. Вставить промпт → дать имя → при необходимости приложить файлы-знания (только методология фирмы).
3. Сохранить. Дальше кидаете агенту входные данные.

## Три честных правила (рамка для всей библиотеки)

1. **Агент оформляет черновик — человек проверяет.** ИИ не выносит итоговое суждение и не выдумывает НПА.
2. **Числа существенности — формула, не ИИ.**
3. **Данные обезличиваем перед облаком.** Свободный текст проверяем глазами.

