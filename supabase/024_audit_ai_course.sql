## Как обезличить: два пути

**Путь 1 — макрос `Анонимизация.bas`** (две команды):

| Команда | Что делает |
|---|---|
| `Обезличить` | выделяете диапазон → вводите префикс токена → значения заменяются токенами, создаётся лист **«Карта_замен»** |
| `ВернутьДанные` | на ответе с токенами делает обратную подстановку оригиналов |

Как завести макрос: открыть файл → `Alt+F11` → File → Import File → выбрать `.bas` → закрыть → `Alt+F8` → выбрать процедуру → Run.

**Путь 2 — вручную «Найти и заменить»** (если макросы заблокированы корп-политикой): список реальных значений → заменить на токены → карту замен вести в отдельном файле. Медленнее, безопасность та же.

## Демо (по шагам)

1. `Обезличить` на табличке → данные заменяются токенами, появляется «Карта_замен».
2. Подчёркиваем: **лист «Карта_замен» в нейросеть не загружаем**.
3. `ВернутьДанные` на ответе с токенами → оригиналы на месте.

## Честный нюанс (обязательно)

Макрос маскирует **значение ячейки целиком**. Если контрагент или УНП сидят **внутри длинной строки** (содержание проводки, субконто) — вкрапление он не поймает. **Свободный текст проверяем глазами.**

## Контроль обезличивания в команде (управленческий взгляд)

- **Правило по умолчанию:** данные клиента в бесплатные ИИ-сервисы не грузим — только обезличенные.
- **Единый инструмент:** вся команда пользуется одним макросом/инструкцией, чтобы не было «кто как умеет».
- **Проверка:** выборочно смотреть, что уходит в облако; карта замен хранится локально, не в переписке.
- Это ложится в **ИИ-политику фирмы** — соберём её в Модуле 5.

