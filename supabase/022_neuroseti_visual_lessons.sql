## Референсы: добавляем в кадр объекты и персонажей

Референс — это картинка-образец, которую вы **прикрепляете к запросу** (в Gemini — прямо в чат), чтобы ИИ взял из неё **конкретный элемент**, а не выдумывал свой. Nano Banana 2 принимает **до 14 референсов** и собирает из них одну сцену.

### Как работать с референсами в промпте

Главное правило: **пронумеруйте прикреплённые картинки и в промпте укажите, что с какого изображения брать и куда добавлять.**

- ❌ «Собери людей и чемодан вместе.»
- ✅ «Возьми персонажей с изображения 1 (левый держит сигарету во рту, правый держит чемодан в руках), чемодан — с изображения 2, помести всех в интерьер с изображения 3.»

Полезные формулировки:
- «Персонаж — с изображения 1, фон — с изображения 2, стиль — с изображения 3.»
- «Добавь объект с изображения 2 в правую руку человека с изображения 1, сохрани его лицо и позу.»
- «Оставь всё как на изображении 1, но замени фон на изображение 2.»

Так в кадр попадает именно ваш товар, ваше лицо или нужный предмет — без «похоже, но не то». Тот же приём мы используем дальше, когда **одеваем модель** и **вкладываем ей в руки продукт**.

`[СКРИН: референсы (2 персонажа + фото чемодана) → собранная из них сцена]`

