## Эскизы: задаём компоновку кадра

Иногда словами трудно объяснить, **где что должно стоять** в кадре. Тогда проще нарисовать: грубый набросок в Paint (или на бумаге и сфотографировать) — прямоугольники и линии с подписями «lamp», «suitcase», «table». Этот эскиз вы **прикрепляете как ещё один референс** — референс компоновки.

### Как меняется промпт с эскизом

Эскиз берёт на себя **композицию** (где что), а промпт — то, **как это выглядит** (стиль, свет, детали). Поэтому в промпте:
- сошлитесь на набросок: «расставь объекты по компоновке с изображения 1 (эскиз)»;
- опишите, чем «оживить» каждый блок: «на месте прямоугольника „suitcase“ — кожаный чемодан, „lamp“ — тёплый настольный светильник»;
- задайте стиль и свет словами — эскиз их не содержит.

Подписывайте элементы на эскизе словами — модель читает надписи прямо с картинки.

