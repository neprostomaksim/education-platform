## Как ИИ понимает картинку: анатомия визуального промпта

Промпт для картинки — это **бриф арт-директору.** Чем конкретнее вы опишете, что в кадре, тем лучше результат. Слова «красиво», «классно», «качественно» модель почти игнорирует — их нужно заменять **конкретными визуальными признаками.**

Хороший визуальный промпт собирается из блоков (не все обязательны, но идут в логическом порядке):

| Блок | Что описать |
|---|---|
| **Субъект** | кто/что, действие, эмоция, детали (одежда, возраст, материалы) |
| **Окружение/фон** | где, обстановка, погода, эпоха |
| **Композиция и кадр** | план (крупный/средний/общий), ракурс, расположение, глубина |
| **Свет** | источник, направление, время суток, мягкий/жёсткий, контровой |
| **Стиль/медиум** | фото, иллюстрация, 3D, аниме; для фото — камера, объектив, плёнка |
| **Цвет и настроение** | палитра, температура, атмосфера |
| **Текст на картинке** | дословно в кавычках, где и как |

