## Концепция и сценарий ролика

Нейросеть — это оператор и актёр, но **режиссёр — вы.** Если генерить «наугад», выйдет красивый, но бессмысленный набор кадров. Поэтому сначала — план.

**Как собирается ролик.** Видеомодель генерит короткие кусочки — обычно по **4–8 секунд** за раз. Значит, ролик на 15–30 секунд — это **4–6 коротких кадров**, которые мы потом склеим. Заранее нужно понять:

- **Концепция:** идея, **хук** (первые 3 секунды), посыл, формат, длина.
- **Раскадровка:** что в каждом кадре, какое движение, какой звук.

Удержание начинается с первого кадра: хук сразу, динамика, смена планов — без «раскачки».

