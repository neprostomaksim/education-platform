## Фотосессия и AI-модель

Самое ценное — **серия кадров одного и того же персонажа или продукта.** Так и делаются нейрофотосессии.

### Правило консистентности

1. Опишите субъект **один раз и максимально детально** — это «карточка персонажа»: возраст, лицо, волосы, телосложение, одежда, свет.
2. Переиспользуйте эту карточку **дословно** в каждом кадре.
3. Меняйте **одну ось за раз**: поза ИЛИ ракурс ИЛИ одежда ИЛИ локация ИЛИ свет. Остальное держите неизменным.
4. Формулировка кадра: «тот же [субъект], что на кадре 1, но [одно изменение]».

### AI-модель: генерируем эталонное лицо

Фотосессию удобно начать с **эталонного лица** модели — и дальше держать его одинаковым во всех кадрах. Главная беда здесь — **эффект «пластика»**: он появляется от неправильного света и параметров. Чтобы лицо было живым и качественным:

| Параметр | Рекомендация |
|---|---|
| **Фон** | светлый однотонный (белый/светло-серый) — лицо в фокусе, легко переносить в другие сцены |
| **Кадр / дальность** | крупный или поясной портрет, лицо занимает бóльшую часть кадра, взгляд в камеру |
| **Освещение** | мягкое рассеянное (софтбокс или большое окно) под ~45°, лёгкий заполняющий свет; **без жёсткой вспышки в лоб и плоского фронтального света** — именно они дают пластик |
| **Объектив** | 85mm, малая глубина резкости (портретное «боке») |
| **Реализм** | видимая текстура кожи, естественные несовершенства, тонкое плёночное зерно; никакой «глянцево-3D» кожи |

